/// Spawn a mitmdump process, wire up stdout/stderr monitors, and wait for
/// it to become ready. This is a free function so it can run in a
/// `tokio::spawn` without borrowing `MitmProxy`.
pub(crate) async fn spawn_mitmdump(
    config: &ProxyConfig,
    port: u16,
    crash_tx: &mpsc::Sender<()>,
    stopping: &Arc<AtomicBool>,
    usage_state_id: &str,
) -> RunnerResult<tokio::process::Child> {
    let mut cmd = tokio::process::Command::new(&config.mitmdump_bin);
    cmd.arg("--mode")
        .arg("transparent")
        .arg("--listen-port")
        .arg(port.to_string())
        .arg("--set")
        .arg(format!("confdir={}", config.ca_dir.display()))
        .arg("--set")
        .arg(format!(
            "vm0_proxy_registry_path={}",
            config.registry_path.display()
        ))
        .arg("--set")
        .arg(format!("vm0_usage_state_id={usage_state_id}"))
        .arg("--scripts")
        .arg(config.addon_dir.join("mitm_addon.py"))
        .arg("--set")
        .arg("flow_detail=0")
        .arg("--set")
        .arg("termlog_verbosity=warn")
        .arg("--set")
        .arg(format!(
            "ssl_verify_upstream_trusted_ca={}",
            crate::deps::SYSTEM_CA_BUNDLE
        ));
    if let Some(url) = &config.api_url {
        cmd.arg("--set").arg(format!("vm0_api_url={url}"));
    }
    cmd.stdin(std::process::Stdio::null())
        .stdout(std::process::Stdio::piped())
        .stderr(std::process::Stdio::piped());
    cmd.kill_on_drop(true);

    // SAFETY: `set_pdeathsig` calls `prctl(PR_SET_PDEATHSIG)` which is
    // async-signal-safe. This ensures the kernel sends SIGKILL to the
    // mitmdump child when the parent runner process dies, preventing
    // orphaned processes after runner crashes or restarts.
    unsafe {
        cmd.pre_exec(|| {
            nix::sys::prctl::set_pdeathsig(nix::sys::signal::Signal::SIGKILL)
                .map_err(std::io::Error::from)
        });
    }

    info!(port, bin = %config.mitmdump_bin.display(), "starting mitmdump");

    let mut child = spawn_mitmdump_child(&mut cmd, &config.mitmdump_bin).await?;

    // Stream stdout to tracing; when the pipe closes (process exited),
    // send a crash notification unless we're in a graceful stop.
    if let Some(stdout) = child.stdout.take() {
        let crash_tx = crash_tx.clone();
        let stopping = Arc::clone(stopping);
        tokio::spawn(async move {
            let mut lines = tokio::io::BufReader::new(stdout).lines();
            while let Ok(Some(line)) = lines.next_line().await {
                if !line.is_empty() {
                    info!(target: "mitmdump", "{line}");
                }
            }
            // Pipe closed — process exited.
            if !stopping.load(Ordering::Acquire) {
                let _ = crash_tx.send(()).await;
            }
        });
    }
    if let Some(stderr) = child.stderr.take() {
        tokio::spawn(async move {
            let mut lines = tokio::io::BufReader::new(stderr).lines();
            while let Ok(Some(line)) = lines.next_line().await {
                if !line.is_empty() {
                    warn!(target: "mitmdump", "stderr: {line}");
                }
            }
        });
    }

    // Wait for process to be alive (poll liveness).
    if let Err(e) = wait_for_ready(&mut child, port, READY_TIMEOUT).await {
        stopping.store(true, Ordering::Release);
        let _ = child.kill().await;
        return Err(e);
    }

    Ok(child)
}

fn is_text_file_busy(error: &std::io::Error) -> bool {
    error.raw_os_error() == Some(nix::libc::ETXTBSY)
}

async fn spawn_mitmdump_child(
    cmd: &mut tokio::process::Command,
    bin: &Path,
) -> RunnerResult<tokio::process::Child> {
    spawn_mitmdump_child_with_retry(cmd, bin, TEXT_BUSY_SPAWN_RETRY_DELAY, |attempt, error| {
        warn!(
            attempt,
            bin = %bin.display(),
            error = %error,
            "mitmdump binary is text-busy, retrying spawn"
        );
    })
    .await
}

async fn spawn_mitmdump_child_with_retry(
    cmd: &mut tokio::process::Command,
    bin: &Path,
    retry_delay: Duration,
    on_text_busy: impl FnMut(usize, &std::io::Error),
) -> RunnerResult<tokio::process::Child> {
    retry_text_busy_spawn(bin, retry_delay, || cmd.spawn(), on_text_busy).await
}

async fn retry_text_busy_spawn<T>(
    bin: &Path,
    retry_delay: Duration,
    mut spawn: impl FnMut() -> std::io::Result<T>,
    mut on_text_busy: impl FnMut(usize, &std::io::Error),
) -> RunnerResult<T> {
    let mut attempt = 0usize;
    loop {
        match spawn() {
            Ok(child) => return Ok(child),
            Err(error) if is_text_file_busy(&error) && attempt < TEXT_BUSY_SPAWN_MAX_RETRIES => {
                attempt += 1;
                on_text_busy(attempt, &error);
                tokio::time::sleep(retry_delay).await;
            }
            Err(error) => {
                return Err(RunnerError::Internal(format!(
                    "spawn mitmdump {}: {error}",
                    bin.display()
                )));
            }
        }
    }
}

/// Wait for mitmdump to start listening on `port` (TCP connect probe).
async fn wait_for_ready(
    child: &mut tokio::process::Child,
    port: u16,
    timeout: Duration,
) -> RunnerResult<()> {
    let poll_interval = Duration::from_millis(200);
    let start = std::time::Instant::now();
    let addr = std::net::SocketAddr::from(([127, 0, 0, 1], port));

    while start.elapsed() < timeout {
        // Check if process died.
        match child.try_wait() {
            Ok(Some(status)) => {
                return Err(RunnerError::Internal(format!(
                    "mitmdump exited immediately with {}",
                    status
                        .code()
                        .map_or("unknown".to_string(), |c| c.to_string()),
                )));
            }
            Ok(None) => {}
            Err(e) => {
                return Err(RunnerError::Internal(format!(
                    "mitmdump process check: {e}"
                )));
            }
        }
        // Probe TCP port.
        if tokio::net::TcpStream::connect(addr).await.is_ok() {
            return Ok(());
        }
        tokio::time::sleep(poll_interval).await;
    }

    Err(RunnerError::Internal(format!(
        "mitmdump did not start listening on port {port} within {}s",
        timeout.as_secs()
    )))
}

/// Find an available TCP port by binding to port 0.
fn find_available_port() -> RunnerResult<u16> {
    let listener = std::net::TcpListener::bind("0.0.0.0:0")
        .map_err(|e| RunnerError::Internal(format!("bind port 0: {e}")))?;
    let port = listener
        .local_addr()
        .map_err(|e| RunnerError::Internal(format!("local_addr: {e}")))?
        .port();
    Ok(port)
}

