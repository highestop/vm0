/// Manages the mitmdump process lifecycle and proxy registry.
pub struct MitmProxy {
    port: u16,
    config: ProxyConfig,
    child: Option<tokio::process::Child>,
    /// Sender used by the stdout monitor task to signal unexpected exit.
    crash_tx: mpsc::Sender<()>,
    /// Set to `true` during graceful `stop()` / `Drop` to suppress crash notifications.
    stopping: Arc<AtomicBool>,
    /// Per-mitmdump-process token written by the addon to `usage-pending`.
    usage_state_id: String,
    /// Host timestamp from when `usage_state_id` was minted.
    usage_state_started_at_ms: u64,
    usage_flush_state: Arc<Mutex<UsageFlushTarget>>,
    jsonl_flush_request_lock: Arc<AsyncMutex<()>>,
}

impl MitmProxy {
    /// Prepare the proxy: allocate a port, write addon script and empty registry.
    ///
    /// Returns `(proxy, crash_rx)`. The caller should select on `crash_rx` to
    /// detect unexpected mitmdump exits and trigger a restart.
    pub async fn new(config: ProxyConfig) -> RunnerResult<(Self, mpsc::Receiver<()>)> {
        let port = find_available_port()?;

        // Write addon scripts to directory.
        // Remove-and-recreate to purge stale files from previous binary versions
        // (e.g. a renamed module would leave an orphan .py that Python could import).
        match tokio::fs::remove_dir_all(&config.addon_dir).await {
            Ok(()) => {}
            Err(e) if e.kind() == std::io::ErrorKind::NotFound => {}
            Err(e) => {
                return Err(RunnerError::Internal(format!(
                    "remove stale addon dir {}: {e}",
                    config.addon_dir.display()
                )));
            }
        }
        tokio::fs::create_dir_all(&config.addon_dir)
            .await
            .map_err(|e| RunnerError::Internal(format!("create addon dir: {e}")))?;
        for (name, content) in ADDON_FILES {
            // Addon file names may include subdirectories (e.g.
            // "usage/providers/connectors/x.py") after the usage/ package split.
            // Ensure the parent directory exists before writing.
            let path = config.addon_dir.join(name);
            if let Some(parent) = path.parent() {
                tokio::fs::create_dir_all(parent).await.map_err(|e| {
                    RunnerError::Internal(format!(
                        "create addon parent dir {}: {e}",
                        parent.display()
                    ))
                })?;
            }
            tokio::fs::write(&path, content)
                .await
                .map_err(|e| RunnerError::Internal(format!("write addon {name}: {e}")))?;
        }

        // Write empty registry file.
        let empty_registry = ProxyRegistry {
            vms: HashMap::new(),
            updated_at: 0,
        };
        write_registry(&config.registry_path, &empty_registry).await?;

        let (crash_tx, crash_rx) = mpsc::channel(1);
        let (usage_state_id, usage_state_started_at_ms) = new_usage_state_id();
        let usage_flush_state = Arc::new(Mutex::new(UsageFlushTarget {
            expected_usage_state_id: usage_state_id.clone(),
            usage_state_started_at_ms,
        }));
        let jsonl_flush_request_lock = Arc::new(AsyncMutex::new(()));

        Ok((
            Self {
                port,
                config,
                child: None,
                crash_tx,
                stopping: Arc::new(AtomicBool::new(false)),
                usage_state_id,
                usage_state_started_at_ms,
                usage_flush_state,
                jsonl_flush_request_lock,
            },
            crash_rx,
        ))
    }

    /// Spawn the mitmdump process.
    pub async fn start(&mut self) -> RunnerResult<()> {
        let child = spawn_mitmdump(
            &self.config,
            self.port,
            &self.crash_tx,
            &self.stopping,
            &self.usage_state_id,
        )
        .await?;
        self.child = Some(child);
        info!(port = self.port, "mitmdump started");
        Ok(())
    }

    /// The port mitmdump is listening on.
    pub fn port(&self) -> u16 {
        self.port
    }

    /// Create a cloneable handle for registry operations (register/unregister VMs).
    ///
    /// The handle is `Clone + Send + Sync` and uses file locking for concurrent
    /// access, making it safe to share across executor tasks.
    pub fn registry_handle(&self) -> ProxyRegistryHandle {
        ProxyRegistryHandle {
            registry_path: self.config.registry_path.clone(),
            lock_path: self.config.registry_lock_path.clone(),
        }
    }

    /// Create a cloneable handle for asking the addon to flush accepted JSONL writes.
    pub fn jsonl_flush_handle(&self, request_flush_tx: mpsc::Sender<()>) -> MitmJsonlFlushHandle {
        MitmJsonlFlushHandle {
            addon_dir: self.config.addon_dir.clone(),
            usage_state: Arc::clone(&self.usage_flush_state),
            request_lock: Arc::clone(&self.jsonl_flush_request_lock),
            request_flush_tx,
        }
    }

    /// Register a VM in the proxy registry so the addon can identify its traffic.
    pub async fn register_vm(
        &self,
        source_ip: &str,
        registration: &VmRegistration<'_>,
    ) -> RunnerResult<()> {
        self.registry_handle()
            .register_vm(source_ip, registration)
            .await
    }

    /// Unregister a VM from the proxy registry.
    pub async fn unregister_vm(&self, source_ip: &str) -> RunnerResult<()> {
        self.registry_handle().unregister_vm(source_ip).await
    }

    /// Current mitmdump usage state expected in the usage-pending state file.
    pub fn usage_flush_target(&mut self) -> Option<UsageFlushTarget> {
        let child_exited = match self.child.as_mut()?.try_wait() {
            Ok(Some(status)) => {
                warn!(code = status.code(), "mitmdump exited before usage flush");
                true
            }
            Ok(None) => false,
            Err(e) => {
                warn!(error = %e, "failed to query mitmdump status before usage flush");
                false
            }
        };
        if child_exited {
            self.child = None;
            return None;
        }

        let _child_pid = self.child.as_ref().and_then(|child| child.id())?;
        Some(UsageFlushTarget {
            expected_usage_state_id: self.usage_state_id.clone(),
            usage_state_started_at_ms: self.usage_state_started_at_ms,
        })
    }

    /// Ask the running addon to flush buffered usage before shutdown.
    pub fn request_usage_flush(&mut self) -> bool {
        let Some(child) = self.child.as_mut() else {
            return false;
        };
        match child.try_wait() {
            Ok(Some(status)) => {
                warn!(
                    code = status.code(),
                    "mitmdump exited before usage flush request"
                );
                self.child = None;
                return false;
            }
            Ok(None) => {}
            Err(e) => {
                warn!(error = %e, "failed to query mitmdump status before usage flush request");
            }
        }

        let signaled = send_usage_flush_signal(child);
        if !signaled {
            warn!("failed to request mitmdump usage flush");
            return false;
        }

        match child.try_wait() {
            Ok(Some(status)) => {
                warn!(
                    code = status.code(),
                    "mitmdump exited after usage flush request"
                );
                self.child = None;
                false
            }
            Ok(None) => true,
            Err(e) => {
                warn!(error = %e, "failed to query mitmdump status after usage flush request");
                true
            }
        }
    }

    /// Prepare for restart: kill any lingering child, permanently silence
    /// its stdout monitor, and return fresh parameters for
    /// [`spawn_mitmdump`].
    ///
    /// `stopping` is scoped to a single child. The old flag is set to
    /// `true` (so the old monitor task, which may observe the stdout
    /// pipe close at some arbitrary point after this call returns,
    /// reads "graceful shutdown" and never emits a spurious crash
    /// notification) and the field is replaced with a fresh `Arc` for
    /// the incoming process. The new child's monitor starts from a
    /// clean slate and will detect real crashes.
    ///
    /// The caller should spawn the mitmdump process (potentially in a
    /// background task) and then call `complete_restart` with the result.
    pub async fn begin_restart(&mut self) -> MitmRestartParams {
        // Silence the old monitor permanently: after this call, no one
        // else holds a handle that could reset its `Arc<AtomicBool>`
        // back to `false`, so the monitor is guaranteed to read `true`
        // regardless of scheduling order between `kill().await` and
        // the stdout-pipe drain.
        self.stopping.store(true, Ordering::Release);
        if let Some(ref mut child) = self.child {
            let _ = child.kill().await;
            self.child = None;
        }
        // Fresh flag for the incoming process's monitor.
        let new_stopping = Arc::new(AtomicBool::new(false));
        self.stopping = Arc::clone(&new_stopping);
        let (usage_state_id, usage_state_started_at_ms) = new_usage_state_id();
        self.usage_state_id = usage_state_id.clone();
        self.usage_state_started_at_ms = usage_state_started_at_ms;
        *usage_flush_state_guard(&self.usage_flush_state) = UsageFlushTarget {
            expected_usage_state_id: usage_state_id.clone(),
            usage_state_started_at_ms,
        };
        MitmRestartParams {
            config: self.config.clone(),
            port: self.port,
            crash_tx: self.crash_tx.clone(),
            stopping: new_stopping,
            usage_state_id,
        }
    }

    /// Finish a restart by storing the newly spawned child process.
    pub fn complete_restart(&mut self, child: tokio::process::Child) {
        self.child = Some(child);
    }

    /// Gracefully stop mitmdump (SIGTERM → timeout → SIGKILL).
    pub async fn stop(&mut self) -> RunnerResult<()> {
        self.stopping.store(true, Ordering::Release);
        let Some(ref mut child) = self.child else {
            return Ok(());
        };
        info!("stopping mitmdump");
        send_sigterm(child);

        match tokio::time::timeout(STOP_TIMEOUT, child.wait()).await {
            Ok(Ok(status)) => {
                info!(code = status.code(), "mitmdump stopped");
            }
            Ok(Err(e)) => {
                warn!(error = %e, "mitmdump wait failed");
            }
            Err(_) => {
                warn!("mitmdump did not exit in time, sending SIGKILL");
                let _ = child.kill().await;
            }
        }
        self.child = None;
        Ok(())
    }
}

fn now_millis() -> u64 {
    SystemTime::now()
        .duration_since(UNIX_EPOCH)
        .map(|d| d.as_millis().min(u128::from(u64::MAX)) as u64)
        .unwrap_or(0)
}

fn new_usage_state_id() -> (String, u64) {
    (Uuid::new_v4().to_string(), now_millis())
}

fn usage_flush_state_guard(
    usage_state: &Arc<Mutex<UsageFlushTarget>>,
) -> std::sync::MutexGuard<'_, UsageFlushTarget> {
    match usage_state.lock() {
        Ok(guard) => guard,
        Err(poisoned) => {
            warn!("usage flush state lock was poisoned, continuing with inner state");
            poisoned.into_inner()
        }
    }
}

