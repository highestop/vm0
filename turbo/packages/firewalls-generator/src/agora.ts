/**
 * Generate Agora firewall config.
 *
 * Data source: https://docs.agora.io/en/video-calling/channel-management-api/restful-authentication
 *
 * Agora RESTful APIs use HTTP Basic auth with the Customer ID as the
 * username and the Customer Secret as the password.
 */

import { writeOutput } from "./codegen";

const DOCS_URL =
  "https://docs.agora.io/en/video-calling/channel-management-api/restful-authentication";
const CUSTOMER_ID_PLACEHOLDER = "4259477b8362c0ffee5afe10ca1c0ff";
const CUSTOMER_SECRET_PLACEHOLDER = "c0ffee5afe10ca1c0ffee5afe10ca1c";

function generateTypeScript(): string {
  const lines: string[] = [
    "// Auto-generated from Agora RESTful API docs.",
    `// Source: ${DOCS_URL}`,
    "// Regenerate: cd turbo && pnpm -F @vm0/firewalls-generator generate:agora",
    "//",
    "// DO NOT EDIT THIS FILE MANUALLY.",
    "",
    'import type { FirewallConfig } from "../firewall-types";',
    "",
    "export const agoraFirewall = {",
    '  name: "agora",',
    '  description: "Agora RESTful API",',
    "  placeholders: {",
    `    AGORA_CUSTOMER_ID: "${CUSTOMER_ID_PLACEHOLDER}",`,
    `    AGORA_CUSTOMER_SECRET: "${CUSTOMER_SECRET_PLACEHOLDER}",`,
    "  },",
    "  apis: [",
    "    {",
    '      base: "https://api.agora.io",',
    "      auth: {",
    "        headers: {",
    "          Authorization:",
    '            "${{ basic(secrets.AGORA_CUSTOMER_ID, secrets.AGORA_CUSTOMER_SECRET) }}",',
    "        },",
    "      },",
    "      permissions: [],",
    "    },",
    "    {",
    '      base: "https://api.sd-rtn.com",',
    "      auth: {",
    "        headers: {",
    "          Authorization:",
    '            "${{ basic(secrets.AGORA_CUSTOMER_ID, secrets.AGORA_CUSTOMER_SECRET) }}",',
    "        },",
    "      },",
    "      permissions: [],",
    "    },",
    "  ],",
    "} as const satisfies FirewallConfig;",
    "",
  ];

  return lines.join("\n");
}

export async function generate(): Promise<void> {
  console.error("Generating Agora firewall config...");
  const ts = generateTypeScript();
  writeOutput("agora", ts, import.meta.dirname);
}
