/**
 * Generate Airtable firewall config.
 *
 * Data source: https://airtable.com/developers/web/api/authentication
 *
 * Airtable is a cloud collaboration platform.
 */

import { writeOutput } from "./codegen";

const DOCS_URL = "https://airtable.com/developers/web/api/authentication";
// Format: pat + 14 alphanumeric + . + 64 hex (gitleaks: airtable-personnal-access-token)
// e.g. patAbCdEfGhIjKl.0123456789abcdef...
const PLACEHOLDER_VALUE =
  "patCoffeeSafeL0.c0ffee5afe10ca1c0ffee5afe10ca1c0ffee5afe10ca1c0ffee5afe10ca1c0ff";

function generateTypeScript(): string {
  const lines: string[] = [
    "// Auto-generated from Airtable API docs.",
    `// Source: ${DOCS_URL}`,
    "// Regenerate: cd turbo && pnpm -F @vm0/firewalls-generator generate:airtable",
    "//",
    "// DO NOT EDIT THIS FILE MANUALLY.",
    "",
    'import type { FirewallConfig } from "../firewall-types";',
    "",
    "export const airtableFirewall = {",
    '  name: "airtable",',
    '  description: "Airtable API",',
    "  placeholders: {",
    `    AIRTABLE_TOKEN: "${PLACEHOLDER_VALUE}",`,
    "  },",
    "  apis: [",
    "    {",
    '      base: "https://api.airtable.com",',
    "      auth: {",
    "        headers: {",
    '          Authorization: "Bearer ${{ secrets.AIRTABLE_TOKEN }}",',
    "        },",
    "      },",
    "      permissions: [],",
    "    },",
    "  ],",
    "} as const satisfies FirewallConfig;",
    "",
  ];

  return lines.join("\n");
}

export async function generate(): Promise<void> {
  console.error("Generating Airtable firewall config...");
  const ts = generateTypeScript();
  writeOutput("airtable", ts, import.meta.dirname);
}
