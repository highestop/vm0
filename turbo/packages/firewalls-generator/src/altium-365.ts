/**
 * Generate Altium 365 firewall config.
 *
 * Data source: https://www.altium.com/documentation/altium-365/requirements-systems-portal/rest-python-api-documentation
 *
 * Altium 365's Requirements & Systems Portal REST API uses Bearer
 * authentication with a User Token (3-month TTL). Each customer
 * workspace has its own subdomain on *.365.altium.com, so the entire
 * base URL is supplied as a variable.
 */

import { writeOutput } from "./codegen";

const DOCS_URL =
  "https://www.altium.com/documentation/altium-365/requirements-systems-portal/rest-python-api-documentation";
const TOKEN_PLACEHOLDER = "CoffeeSafeLocalCoffeeSafeLocalCoffeeSafe";

function generateTypeScript(): string {
  const lines: string[] = [
    "// Auto-generated from Altium 365 API docs.",
    `// Source: ${DOCS_URL}`,
    "// Regenerate: cd turbo && pnpm -F @vm0/firewalls-generator generate:altium-365",
    "//",
    "// DO NOT EDIT THIS FILE MANUALLY.",
    "",
    'import type { FirewallConfig } from "../firewall-types";',
    "",
    "export const altium365Firewall = {",
    '  name: "altium-365",',
    '  description: "Altium 365 Requirements & Systems Portal REST API",',
    "  placeholders: {",
    `    ALTIUM365_TOKEN: "${TOKEN_PLACEHOLDER}",`,
    "  },",
    "  apis: [",
    "    {",
    '      base: "${{ vars.ALTIUM365_WORKSPACE_URL }}",',
    "      auth: {",
    "        headers: {",
    '          Authorization: "Bearer ${{ secrets.ALTIUM365_TOKEN }}",',
    "        },",
    "      },",
    "      permissions: [],",
    "    },",
    "  ],",
    "} as const satisfies FirewallConfig;",
    "",
  ];

  return lines.join("\n");
}

export async function generate(): Promise<void> {
  console.error("Generating Altium 365 firewall config...");
  const ts = generateTypeScript();
  writeOutput("altium-365", ts, import.meta.dirname);
}
