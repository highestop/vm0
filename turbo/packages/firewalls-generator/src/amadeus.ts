/**
 * Generate Amadeus for Developers firewall config.
 *
 * Data source: https://amadeus4dev.github.io/developer-guides/API-Keys/authorization/
 *
 * Amadeus uses the OAuth 2.0 client credentials grant. This connector exposes
 * static API key and API secret values; callers request access tokens from
 * Amadeus at runtime.
 */

import { writeOutput } from "./codegen";

const DOCS_URL =
  "https://amadeus4dev.github.io/developer-guides/API-Keys/authorization/";
const API_KEY_PLACEHOLDER = "Coffee5afe10ca1Coffee5afe10ca1Co";
const API_SECRET_PLACEHOLDER = "Safe10ca1Coffee5afe10ca1Coffee5afe";

function generateTypeScript(): string {
  const lines: string[] = [
    "// Auto-generated from Amadeus for Developers docs.",
    `// Source: ${DOCS_URL}`,
    "// Regenerate: cd turbo && pnpm -F @vm0/firewalls-generator generate:amadeus",
    "//",
    "// DO NOT EDIT THIS FILE MANUALLY.",
    "",
    'import type { FirewallConfig } from "../firewall-types";',
    "",
    "export const amadeusFirewall = {",
    '  name: "amadeus",',
    '  description: "Amadeus for Developers API",',
    "  placeholders: {",
    `    AMADEUS_API_KEY: "${API_KEY_PLACEHOLDER}",`,
    `    AMADEUS_API_SECRET: "${API_SECRET_PLACEHOLDER}",`,
    "  },",
    "  apis: [",
    "    {",
    '      base: "https://test.api.amadeus.com",',
    "      auth: {},",
    "      permissions: [],",
    "    },",
    "    {",
    '      base: "https://api.amadeus.com",',
    "      auth: {},",
    "      permissions: [],",
    "    },",
    "  ],",
    "} as const satisfies FirewallConfig;",
    "",
  ];
  return lines.join("\n");
}

export async function generate(): Promise<void> {
  console.error("Generating Amadeus for Developers firewall config...");
  const ts = generateTypeScript();
  writeOutput("amadeus", ts, import.meta.dirname);
}
