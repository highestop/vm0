/**
 * Generate BentoML/BentoCloud firewall config.
 *
 * Data sources:
 * - https://docs.bentoml.com/en/latest/scale-with-bentocloud/manage-api-tokens.html
 * - https://docs.bentoml.com/en/latest/scale-with-bentocloud/deployment/call-deployment-endpoints.html
 *
 * BentoCloud uses Bearer token authentication for both the BentoCloud API
 * endpoint and protected deployment endpoints.
 */

import { writeOutput } from "./codegen";

const API_TOKENS_URL =
  "https://docs.bentoml.com/en/latest/scale-with-bentocloud/manage-api-tokens.html";
const DEPLOYMENT_ENDPOINTS_URL =
  "https://docs.bentoml.com/en/latest/scale-with-bentocloud/deployment/call-deployment-endpoints.html";
const PLACEHOLDER_VALUE = "cur7hCoffeeSafeLocalCoffeeSafeLocalCoffeeSafe";

function generateTypeScript(): string {
  const lines: string[] = [
    "// Auto-generated from BentoML/BentoCloud docs.",
    `// Sources: ${API_TOKENS_URL}`,
    `//          ${DEPLOYMENT_ENDPOINTS_URL}`,
    "// Regenerate: cd turbo && pnpm -F @vm0/firewalls-generator generate:bentoml",
    "//",
    "// DO NOT EDIT THIS FILE MANUALLY.",
    "",
    'import type { FirewallConfig } from "../firewall-types";',
    "",
    "export const bentomlFirewall = {",
    '  name: "bentoml",',
    '  description: "BentoCloud API and protected deployment endpoints",',
    "  placeholders: {",
    `    BENTO_CLOUD_API_KEY: "${PLACEHOLDER_VALUE}",`,
    "  },",
    "  apis: [",
    "    {",
    '      base: "${{ vars.BENTO_CLOUD_API_ENDPOINT }}",',
    "      auth: {",
    "        headers: {",
    '          Authorization: "Bearer ${{ secrets.BENTO_CLOUD_API_KEY }}",',
    "        },",
    "      },",
    "      permissions: [],",
    "    },",
    "    {",
    '      base: "https://{deployment+}.bentoml.ai",',
    "      auth: {",
    "        headers: {",
    '          Authorization: "Bearer ${{ secrets.BENTO_CLOUD_API_KEY }}",',
    "        },",
    "      },",
    "      permissions: [],",
    "    },",
    "    {",
    '      base: "https://{deployment+}.cloud-apps.bentoml.com",',
    "      auth: {",
    "        headers: {",
    '          Authorization: "Bearer ${{ secrets.BENTO_CLOUD_API_KEY }}",',
    "        },",
    "      },",
    "      permissions: [],",
    "    },",
    "  ],",
    "} as const satisfies FirewallConfig;",
    "",
  ];

  return lines.join("\n");
}

export async function generate(): Promise<void> {
  console.error("Generating BentoML firewall config...");
  const ts = generateTypeScript();
  writeOutput("bentoml", ts, import.meta.dirname);
}
