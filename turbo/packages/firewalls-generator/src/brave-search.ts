/**
 * Brave Search API firewall config generator.
 *
 * Data source: https://api-dashboard.search.brave.com/documentation/guides/authentication
 *
 * Brave Search uses a custom X-Subscription-Token header (not Bearer).
 * API key format: BSAI prefix + 20+ alphanumeric/dash/underscore chars.
 */

import { writeOutput } from "./codegen";

const DOCS_URL =
  "https://api-dashboard.search.brave.com/documentation/guides/authentication";
// Format: BSAI + 20+ chars [a-zA-Z0-9_-]
const PLACEHOLDER_VALUE = "BSAIVm0PlaceHolder00000000";

function generateTypeScript(): string {
  const lines: string[] = [
    "// Auto-generated from Brave Search API docs.",
    `// Source: ${DOCS_URL}`,
    "// Regenerate: cd turbo && pnpm -F @vm0/firewalls-generator generate:brave-search",
    "//",
    "// DO NOT EDIT THIS FILE MANUALLY.",
    "",
    'import type { FirewallConfig } from "../contracts/firewalls";',
    "",
    "export const braveSearchFirewall: FirewallConfig = {",
    '  name: "brave-search",',
    '  description: "Brave Search API",',
    "  placeholders: {",
    `    BRAVE_API_KEY: "${PLACEHOLDER_VALUE}",`,
    "  },",
    "  apis: [",
    "    {",
    '      base: "https://api.search.brave.com",',
    "      auth: {",
    "        headers: {",
    '          "X-Subscription-Token": "${{ secrets.BRAVE_API_KEY }}",',
    "        },",
    "      },",
    "      permissions: [],",
    "    },",
    "  ],",
    "};",
    "",
  ];
  return lines.join("\n");
}

export async function generate(): Promise<void> {
  console.error("Generating Brave Search firewall config...");
  const ts = generateTypeScript();
  writeOutput("brave-search", ts, import.meta.dirname);
}
