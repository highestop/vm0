/**
 * Generate Bright Data firewall config.
 *
 * Data source: https://docs.brightdata.com/api-reference
 *
 * Bright Data is a web scraping and data collection platform.
 * Uses Bearer token authentication.
 * Token format: 64-character hex string, no prefix.
 */

import { writeOutput } from "./codegen";

const DOCS_URL = "https://docs.brightdata.com/api-reference";
// Token is a 64-char hex string; using a realistic-looking placeholder
const PLACEHOLDER_VALUE =
  "c0ffee5afe10ca1c0ffee5afe10ca1c0ffee5afe10ca1c0ffee5afe10ca1c0ff";

function generateTypeScript(): string {
  const lines: string[] = [
    "// Auto-generated from Bright Data API docs.",
    `// Source: ${DOCS_URL}`,
    "// Regenerate: cd turbo && pnpm -F @vm0/firewalls-generator generate:bright-data",
    "//",
    "// DO NOT EDIT THIS FILE MANUALLY.",
    "",
    'import type { FirewallConfig } from "../firewall-types";',
    "",
    "export const brightDataFirewall = {",
    '  name: "bright-data",',
    '  description: "Bright Data API",',
    "  placeholders: {",
    `    BRIGHTDATA_TOKEN: "${PLACEHOLDER_VALUE}",`,
    "  },",
    "  apis: [",
    "    {",
    '      base: "https://api.brightdata.com",',
    "      auth: {",
    "        headers: {",
    '          Authorization: "Bearer ${{ secrets.BRIGHTDATA_TOKEN }}",',
    "        },",
    "      },",
    "      permissions: [],",
    "    },",
    "  ],",
    "} as const satisfies FirewallConfig;",
    "",
  ];

  return lines.join("\n");
}

export async function generate(): Promise<void> {
  console.error("Generating Bright Data firewall config...");
  const ts = generateTypeScript();
  writeOutput("bright-data", ts, import.meta.dirname);
}
