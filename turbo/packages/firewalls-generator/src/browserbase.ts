/**
 * Generate Browserbase firewall config.
 *
 * Data source: https://docs.browserbase.com/reference/api/overview
 *
 * Browserbase is a cloud browser automation platform.
 * Uses custom X-BB-API-Key header authentication.
 * Token format: opaque string, no known prefix.
 */

import { writeOutput } from "./codegen";

const DOCS_URL = "https://docs.browserbase.com/reference/api/overview";
// Token format not publicly documented; using generic placeholder
const PLACEHOLDER_VALUE = "bb_vm0placeholder_000000000000000000000000000000a";

function generateTypeScript(): string {
  const lines: string[] = [
    "// Auto-generated from Browserbase API docs.",
    `// Source: ${DOCS_URL}`,
    "// Regenerate: cd turbo && pnpm -F @vm0/firewalls-generator generate:browserbase",
    "//",
    "// DO NOT EDIT THIS FILE MANUALLY.",
    "",
    'import type { FirewallConfig } from "../contracts/firewalls";',
    "",
    "export const browserbaseFirewall: FirewallConfig = {",
    '  name: "browserbase",',
    '  description: "Browserbase API",',
    "  placeholders: {",
    `    BROWSERBASE_TOKEN: "${PLACEHOLDER_VALUE}",`,
    "  },",
    "  apis: [",
    "    {",
    '      base: "https://api.browserbase.com",',
    "      auth: {",
    "        headers: {",
    '          "X-BB-API-Key": "${{ secrets.BROWSERBASE_TOKEN }}",',
    "        },",
    "      },",
    "      permissions: [],",
    "    },",
    "  ],",
    "};",
    "",
  ];

  return lines.join("\n");
}

export async function generate(): Promise<void> {
  console.error("Generating Browserbase firewall config...");
  const ts = generateTypeScript();
  writeOutput("browserbase", ts, import.meta.dirname);
}
