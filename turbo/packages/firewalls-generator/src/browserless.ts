/**
 * Generate Browserless firewall config.
 *
 * Data source: https://docs.browserless.io/rest-apis/intro
 *
 * Browserless is a headless browser automation platform.
 * Authenticates via the `token` URL query parameter.
 * Token format: UUID v4 (e.g., 094632bb-e326-4c63-b953-82b55700b14c).
 * Three regional base URLs: production-sfo, production-lon, production-ams.
 */

import { writeOutput } from "./codegen";

const DOCS_URL = "https://docs.browserless.io/rest-apis/intro";
// Token is UUID v4; using a realistic-looking placeholder
const PLACEHOLDER_VALUE = "c0ffee5a-fe10-ca1c-0ffe-e5afe10ca1c0";

const REGIONS = ["sfo", "lon", "ams"] as const;

function generateTypeScript(): string {
  const apiEntries = REGIONS.map(
    (region) => `    {
      base: "https://production-${region}.browserless.io",
      auth: {
        headers: {},
        query: {
          token: "$\{{ secrets.BROWSERLESS_TOKEN }}",
        },
      },
      permissions: [],
    }`,
  );

  const lines: string[] = [
    "// Auto-generated from Browserless API docs.",
    `// Source: ${DOCS_URL}`,
    "// Regenerate: cd turbo && pnpm -F @vm0/firewalls-generator generate:browserless",
    "//",
    "// DO NOT EDIT THIS FILE MANUALLY.",
    "",
    'import type { FirewallConfig } from "../contracts/firewalls";',
    "",
    "export const browserlessFirewall = {",
    '  name: "browserless",',
    '  description: "Browserless API",',
    "  placeholders: {",
    `    BROWSERLESS_TOKEN: "${PLACEHOLDER_VALUE}",`,
    "  },",
    "  apis: [",
    apiEntries.join(",\n"),
    "  ],",
    "} as const satisfies FirewallConfig;",
    "",
  ];

  return lines.join("\n");
}

export async function generate(): Promise<void> {
  console.error("Generating Browserless firewall config...");
  const ts = generateTypeScript();
  writeOutput("browserless", ts, import.meta.dirname);
}
