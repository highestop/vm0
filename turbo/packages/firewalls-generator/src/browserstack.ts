/**
 * Generate BrowserStack firewall config.
 *
 * Data source: https://www.browserstack.com/docs/automate/api-reference/selenium/authentication
 *
 * BrowserStack uses HTTP Basic auth with username + access key on every
 * surface. Automate (Selenium / web) lives on api.browserstack.com;
 * App Automate (mobile Appium) lives on api-cloud.browserstack.com;
 * the Screenshots API is served from www.browserstack.com.
 */

import { writeOutput } from "./codegen";

const DOCS_URL =
  "https://www.browserstack.com/docs/automate/api-reference/selenium/authentication";
// BrowserStack usernames and access keys are opaque alphanumeric.
const USERNAME_PLACEHOLDER = "CoffeeSafeLocalCoffe";
const ACCESS_KEY_PLACEHOLDER = "CoffeeSafeLocalCoffeeSa";

function generateTypeScript(): string {
  const lines: string[] = [
    "// Auto-generated from BrowserStack API docs.",
    `// Source: ${DOCS_URL}`,
    "// Regenerate: cd turbo && pnpm -F @vm0/firewalls-generator generate:browserstack",
    "//",
    "// DO NOT EDIT THIS FILE MANUALLY.",
    "",
    'import type { FirewallConfig } from "../firewall-types";',
    "",
    "export const browserstackFirewall = {",
    '  name: "browserstack",',
    '  description: "BrowserStack cross-browser and device testing API",',
    "  placeholders: {",
    `    BROWSERSTACK_USERNAME: "${USERNAME_PLACEHOLDER}",`,
    `    BROWSERSTACK_ACCESS_KEY: "${ACCESS_KEY_PLACEHOLDER}",`,
    "  },",
    "  apis: [",
    "    {",
    '      base: "https://api.browserstack.com",',
    "      auth: {",
    "        headers: {",
    "          Authorization:",
    '            "${{ basic(secrets.BROWSERSTACK_USERNAME, secrets.BROWSERSTACK_ACCESS_KEY) }}",',
    "        },",
    "      },",
    "      permissions: [],",
    "    },",
    "    {",
    '      base: "https://api-cloud.browserstack.com",',
    "      auth: {",
    "        headers: {",
    "          Authorization:",
    '            "${{ basic(secrets.BROWSERSTACK_USERNAME, secrets.BROWSERSTACK_ACCESS_KEY) }}",',
    "        },",
    "      },",
    "      permissions: [],",
    "    },",
    "    {",
    '      base: "https://www.browserstack.com",',
    "      auth: {",
    "        headers: {",
    "          Authorization:",
    '            "${{ basic(secrets.BROWSERSTACK_USERNAME, secrets.BROWSERSTACK_ACCESS_KEY) }}",',
    "        },",
    "      },",
    "      permissions: [],",
    "    },",
    "  ],",
    "} as const satisfies FirewallConfig;",
    "",
  ];

  return lines.join("\n");
}

export async function generate(): Promise<void> {
  console.error("Generating BrowserStack firewall config...");
  const ts = generateTypeScript();
  writeOutput("browserstack", ts, import.meta.dirname);
}
