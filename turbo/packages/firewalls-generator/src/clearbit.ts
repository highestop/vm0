/**
 * Generate Clearbit firewall config.
 *
 * Data source: https://help.clearbit.com/hc/en-us/articles/6045527495191-How-Do-I-Access-My-Clearbit-API-Key
 *
 * Clearbit uses a secret API key in the Authorization header. Clearbit's
 * Marketo setup docs show the value as `Bearer sk_your_secret_key`.
 */

import { writeOutput } from "./codegen";

const DOCS_URL =
  "https://help.clearbit.com/hc/en-us/articles/6045527495191-How-Do-I-Access-My-Clearbit-API-Key";
const PLACEHOLDER_VALUE = "sk_c0ffee5afe10ca1c0ffee5afe10ca1c0";

function generateTypeScript(): string {
  const lines: string[] = [
    "// Auto-generated from Clearbit API docs.",
    `// Source: ${DOCS_URL}`,
    "// Regenerate: cd turbo && pnpm -F @vm0/firewalls-generator generate:clearbit",
    "//",
    "// DO NOT EDIT THIS FILE MANUALLY.",
    "",
    'import type { FirewallConfig } from "../firewall-types";',
    "",
    "export const clearbitFirewall = {",
    '  name: "clearbit",',
    '  description: "Clearbit API",',
    "  placeholders: {",
    `    CLEARBIT_TOKEN: "${PLACEHOLDER_VALUE}",`,
    "  },",
    "  apis: [",
    '    "https://person.clearbit.com",',
    '    "https://person-stream.clearbit.com",',
    '    "https://company.clearbit.com",',
    '    "https://reveal.clearbit.com",',
    '    "https://risk.clearbit.com",',
    "  ].map((base) => ({",
    "    base,",
    "    auth: {",
    "      headers: {",
    '        Authorization: "Bearer ${{ secrets.CLEARBIT_TOKEN }}",',
    "      },",
    "    },",
    "    permissions: [],",
    "  })),",
    "} as const satisfies FirewallConfig;",
    "",
  ];
  return lines.join("\n");
}

export async function generate(): Promise<void> {
  console.error("Generating Clearbit firewall config...");
  const ts = generateTypeScript();
  writeOutput("clearbit", ts, import.meta.dirname);
}
