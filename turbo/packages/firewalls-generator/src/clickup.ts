/**
 * Generate ClickUp firewall config.
 *
 * Data source: https://developer.clickup.com/docs/authentication
 *
 * ClickUp is a project management and productivity platform.
 * Personal API tokens use bare Authorization header (no Bearer prefix).
 * Token format: pk_{numericId}_{alphanumeric} (~45 chars).
 */

import { writeOutput } from "./codegen";

const DOCS_URL = "https://developer.clickup.com/docs/authentication";
// Format: pk_{numericId}_{32 uppercase alphanumeric}
// e.g. pk_4753994_EXP7MPOJ7XQM5UJDV2M45MPF0YHH5YHO
const PLACEHOLDER_VALUE = "pk_1001001_COFFEESAFELOCALCOFFEESAFELOCALCOFF";

function generateTypeScript(): string {
  const lines: string[] = [
    "// Auto-generated from ClickUp API docs.",
    `// Source: ${DOCS_URL}`,
    "// Regenerate: cd turbo && pnpm -F @vm0/firewalls-generator generate:clickup",
    "//",
    "// DO NOT EDIT THIS FILE MANUALLY.",
    "",
    'import type { FirewallConfig } from "../contracts/firewalls";',
    "",
    "export const clickupFirewall = {",
    '  name: "clickup",',
    '  description: "ClickUp API",',
    "  placeholders: {",
    `    CLICKUP_TOKEN: "${PLACEHOLDER_VALUE}",`,
    "  },",
    "  apis: [",
    "    {",
    '      base: "https://api.clickup.com/api",',
    "      auth: {",
    "        headers: {",
    '          Authorization: "${{ secrets.CLICKUP_TOKEN }}",',
    "        },",
    "      },",
    "      permissions: [],",
    "    },",
    "  ],",
    "} as const satisfies FirewallConfig;",
    "",
  ];

  return lines.join("\n");
}

export async function generate(): Promise<void> {
  console.error("Generating ClickUp firewall config...");
  const ts = generateTypeScript();
  writeOutput("clickup", ts, import.meta.dirname);
}
