/**
 * CoinGecko API firewall config generator.
 *
 * Data sources:
 * - https://docs.coingecko.com/reference/authentication
 * - https://docs.coingecko.com/v3.0.1/reference/authentication
 *
 * CoinGecko Demo and Pro APIs both recommend header authentication, but use
 * different header names and root URLs.
 */

import { writeOutput } from "./codegen";

const DOCS_URL = "https://docs.coingecko.com/reference/authentication";
// No documented key format; generic alphanumeric.
const PLACEHOLDER_VALUE = "CoffeeSafeLocalCoffeeSafeLocalCoffeeSafeLocalCof";

function generateTypeScript(): string {
  const lines: string[] = [
    "// Auto-generated from CoinGecko API docs.",
    `// Source: ${DOCS_URL}`,
    "// Regenerate: cd turbo && pnpm -F @vm0/firewalls-generator generate:coingecko",
    "//",
    "// DO NOT EDIT THIS FILE MANUALLY.",
    "",
    'import type { FirewallConfig } from "../firewall-types";',
    "",
    "export const coingeckoFirewall = {",
    '  name: "coingecko",',
    '  description: "CoinGecko API",',
    "  placeholders: {",
    `    COINGECKO_TOKEN: "${PLACEHOLDER_VALUE}",`,
    "  },",
    "  apis: [",
    "    {",
    '      base: "https://api.coingecko.com",',
    "      auth: {",
    "        headers: {",
    '          "x-cg-demo-api-key": "${{ secrets.COINGECKO_TOKEN }}",',
    "        },",
    "      },",
    "      permissions: [],",
    "    },",
    "    {",
    '      base: "https://pro-api.coingecko.com",',
    "      auth: {",
    "        headers: {",
    '          "x-cg-pro-api-key": "${{ secrets.COINGECKO_TOKEN }}",',
    "        },",
    "      },",
    "      permissions: [],",
    "    },",
    "  ],",
    "} as const satisfies FirewallConfig;",
    "",
  ];
  return lines.join("\n");
}

export async function generate(): Promise<void> {
  console.error("Generating CoinGecko firewall config...");
  const ts = generateTypeScript();
  writeOutput("coingecko", ts, import.meta.dirname);
}
