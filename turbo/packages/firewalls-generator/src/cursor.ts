/**
 * Generate Cursor firewall config.
 *
 * Data source: https://cursor.com/docs/background-agent/api/overview
 *
 * Cursor Cloud Agents use Bearer token authentication via the Authorization
 * header. A single API host (api.cursor.com) serves the whole REST surface
 * (agents, runs, artifacts, models, repositories, me), so a single host entry
 * covers everything. Token format: `key_`-prefixed string.
 */

import { writeOutput } from "./codegen";

const DOCS_URL = "https://cursor.com/docs/background-agent/api/overview";
// `key_`-prefixed placeholder using the Coffee/Safe/Local hex vocabulary so the
// proxy regex still matches without leaking a real key shape.
const PLACEHOLDER_VALUE =
  "key_c0ffee5afe10ca1c0ffee5afe10ca1c0ffee5afe10ca1c0ffee5afe10ca1c0ff";

function generateTypeScript(): string {
  const lines: string[] = [
    "// Auto-generated from Cursor Cloud Agents API docs.",
    `// Source: ${DOCS_URL}`,
    "// Regenerate: cd turbo && pnpm -F @vm0/firewalls-generator generate:cursor",
    "//",
    "// DO NOT EDIT THIS FILE MANUALLY.",
    "",
    'import type { FirewallConfig } from "../firewall-types";',
    "",
    "export const cursorFirewall = {",
    '  name: "cursor",',
    '  description: "Cursor",',
    "  placeholders: {",
    `    CURSOR_TOKEN: "${PLACEHOLDER_VALUE}",`,
    "  },",
    "  apis: [",
    "    {",
    '      base: "https://api.cursor.com",',
    "      auth: {",
    "        headers: {",
    '          Authorization: "Bearer ${{ secrets.CURSOR_TOKEN }}",',
    "        },",
    "      },",
    "      permissions: [],",
    "    },",
    "  ],",
    "} as const satisfies FirewallConfig;",
    "",
  ];

  return lines.join("\n");
}

export async function generate(): Promise<void> {
  console.error("Generating Cursor firewall config...");
  const ts = generateTypeScript();
  writeOutput("cursor", ts, import.meta.dirname);
}
