/**
 * Generate Customer.io firewall config.
 *
 * Data source: https://docs.customer.io/integrations/api/app/
 *
 * Customer.io is a messaging automation platform.
 * Uses Bearer token authentication with App API Key.
 * App API only — Track API uses Basic auth (site_id:api_key), not supported.
 * Token format: opaque string, no known prefix.
 */

import { writeOutput } from "./codegen";

const DOCS_URL = "https://docs.customer.io/integrations/api/app/";
// Token format not publicly documented; using generic placeholder
const PLACEHOLDER_VALUE =
  "CoffeeSafeLocalCoffeeSafeLocalCoffeeSafeLocalCoffeeSafeLocalCoffeeSafeL";

function generateTypeScript(): string {
  const lines: string[] = [
    "// Auto-generated from Customer.io API docs.",
    `// Source: ${DOCS_URL}`,
    "// Regenerate: cd turbo && pnpm -F @vm0/firewalls-generator generate:customer-io",
    "//",
    "// DO NOT EDIT THIS FILE MANUALLY.",
    "",
    'import type { FirewallConfig } from "../contracts/firewalls";',
    "",
    "export const customerIoFirewall = {",
    '  name: "customer-io",',
    '  description: "Customer.io API",',
    "  placeholders: {",
    `    CUSTOMERIO_APP_TOKEN: "${PLACEHOLDER_VALUE}",`,
    "  },",
    "  apis: [",
    "    {",
    '      base: "https://api.customer.io",',
    "      auth: {",
    "        headers: {",
    '          Authorization: "Bearer ${{ secrets.CUSTOMERIO_APP_TOKEN }}",',
    "        },",
    "      },",
    "      permissions: [],",
    "    },",
    "  ],",
    "} as const satisfies FirewallConfig;",
    "",
  ];

  return lines.join("\n");
}

export async function generate(): Promise<void> {
  console.error("Generating Customer.io firewall config...");
  const ts = generateTypeScript();
  writeOutput("customer-io", ts, import.meta.dirname);
}
