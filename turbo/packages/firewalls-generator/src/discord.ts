/**
 * Generate Discord firewall config.
 *
 * Data source: https://discord.com/developers/docs/reference#authentication
 *
 * Discord is a communication platform for communities.
 * Uses "Bot" authorization prefix (not Bearer).
 */

import { writeOutput } from "./codegen";

const DOCS_URL = "https://discord.com/developers/docs/reference#authentication";
// Format: 3 base64 parts separated by dots (userId.timestamp.hmac)
// e.g. OTM1NTY2MjUwNTg1NzYzODgw.YIz98g.ffPyQbZQGxQ3tmunQx2i86AWT-M
const PLACEHOLDER_VALUE =
  "CoffeeSafeLocalCoffeeSaf.Coffee.CoffeeSafeLocalCoffeeSafeLo";

function generateTypeScript(): string {
  const lines: string[] = [
    "// Auto-generated from Discord API docs.",
    `// Source: ${DOCS_URL}`,
    "// Regenerate: cd turbo && pnpm -F @vm0/firewalls-generator generate:discord",
    "//",
    "// DO NOT EDIT THIS FILE MANUALLY.",
    "",
    'import type { FirewallConfig } from "../firewall-types";',
    "",
    "export const discordFirewall = {",
    '  name: "discord",',
    '  description: "Discord API",',
    "  placeholders: {",
    `    DISCORD_BOT_TOKEN: "${PLACEHOLDER_VALUE}",`,
    "  },",
    "  apis: [",
    "    {",
    '      base: "https://discord.com/api",',
    "      auth: {",
    "        headers: {",
    '          Authorization: "Bot ${{ secrets.DISCORD_BOT_TOKEN }}",',
    "        },",
    "      },",
    "      permissions: [],",
    "    },",
    "  ],",
    "} as const satisfies FirewallConfig;",
    "",
  ];

  return lines.join("\n");
}

export async function generate(): Promise<void> {
  console.error("Generating Discord firewall config...");
  const ts = generateTypeScript();
  writeOutput("discord", ts, import.meta.dirname);
}
