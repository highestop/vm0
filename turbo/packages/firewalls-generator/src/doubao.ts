/**
 * Generate Doubao (Volcengine 豆包语音) firewall config.
 *
 * Data sources:
 * - https://www.volcengine.com/docs/6561/162929 (product index)
 * - https://www.volcengine.com/docs/6561/1598757 (TTS HTTP chunked / SSE)
 * - https://www.volcengine.com/docs/6561/2227958 (voice clone V3)
 * - https://www.volcengine.com/docs/6561/1354868 (ASR file recognition)
 *
 * The new-console flow uses a single header `X-Api-Key: <uuid>` for every
 * endpoint under `openspeech.bytedance.com`. The three endpoints that still
 * require the legacy `X-Api-App-Id` + `X-Api-Access-Key` headers
 * (async long-text TTS, realtime S2S, podcast) are out of scope for this
 * connector — they remain reachable by direct call with separately managed
 * credentials.
 */

import { writeOutput } from "./codegen";

const TTS_DOC_URL = "https://www.volcengine.com/docs/6561/1598757";
const VOICE_CLONE_DOC_URL = "https://www.volcengine.com/docs/6561/2227958";
const ASR_DOC_URL = "https://www.volcengine.com/docs/6561/1354868";
// UUID-shaped placeholder using the Coffee/Safe/Local hex vocabulary so the
// proxy regex still matches without leaking a real key shape.
const PLACEHOLDER_VALUE = "c0ffee5a-fe10-ca1c-0ffe-e5afe10ca1ce";

function generateTypeScript(): string {
  const lines: string[] = [
    "// Auto-generated from Volcengine Doubao speech docs.",
    `// Sources: ${TTS_DOC_URL}`,
    `//          ${VOICE_CLONE_DOC_URL}`,
    `//          ${ASR_DOC_URL}`,
    "// Regenerate: cd turbo && pnpm -F @vm0/firewalls-generator generate:doubao",
    "//",
    "// DO NOT EDIT THIS FILE MANUALLY.",
    "",
    'import type { FirewallConfig } from "../firewall-types";',
    "",
    "export const doubaoFirewall = {",
    '  name: "doubao",',
    '  description: "Volcengine Doubao speech: TTS, ASR, voice cloning",',
    "  placeholders: {",
    `    DOUBAO_API_KEY: "${PLACEHOLDER_VALUE}",`,
    "  },",
    "  apis: [",
    "    {",
    '      base: "https://openspeech.bytedance.com",',
    "      auth: {",
    "        headers: {",
    '          "X-Api-Key": "${{ secrets.DOUBAO_API_KEY }}",',
    "        },",
    "      },",
    "      permissions: [],",
    "    },",
    "  ],",
    "} as const satisfies FirewallConfig;",
    "",
  ];

  return lines.join("\n");
}

export async function generate(): Promise<void> {
  console.error("Generating Doubao firewall config...");
  const ts = generateTypeScript();
  writeOutput("doubao", ts, import.meta.dirname);
}
