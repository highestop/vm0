/**
 * fal.ai API firewall config generator.
 *
 * Data source: https://fal.ai/docs/reference/platform-apis/authentication
 *
 * fal.ai uses a custom "Key" prefix in Authorization header (not Bearer).
 * API key format is not publicly documented; using generic placeholder.
 */

import { writeOutput } from "./codegen";

const DOCS_URL = "https://fal.ai/docs/reference/platform-apis/authentication";
// Format: key_id:key_secret where key_secret has sk_live_ prefix
const PLACEHOLDER_VALUE =
  "CoffeeSafeLocalC:sk_live_CoffeeSafeLocalCoffeeSafeLo";

function generateTypeScript(): string {
  const lines: string[] = [
    "// Auto-generated from fal.ai API docs.",
    `// Source: ${DOCS_URL}`,
    "// Regenerate: cd turbo && pnpm -F @vm0/firewalls-generator generate:fal",
    "//",
    "// DO NOT EDIT THIS FILE MANUALLY.",
    "",
    'import type { FirewallConfig } from "../contracts/firewalls";',
    "",
    "export const falFirewall = {",
    '  name: "fal",',
    '  description: "fal.ai API",',
    "  placeholders: {",
    `    FAL_TOKEN: "${PLACEHOLDER_VALUE}",`,
    "  },",
    "  apis: [",
    "    {",
    '      base: "https://fal.run",',
    "      auth: {",
    "        headers: {",
    '          Authorization: "Key ${{ secrets.FAL_TOKEN }}",',
    "        },",
    "      },",
    "      permissions: [],",
    "    },",
    "    {",
    '      base: "https://queue.fal.run",',
    "      auth: {",
    "        headers: {",
    '          Authorization: "Key ${{ secrets.FAL_TOKEN }}",',
    "        },",
    "      },",
    "      permissions: [],",
    "    },",
    "    {",
    '      base: "https://api.fal.ai",',
    "      auth: {",
    "        headers: {",
    '          Authorization: "Key ${{ secrets.FAL_TOKEN }}",',
    "        },",
    "      },",
    "      permissions: [],",
    "    },",
    "  ],",
    "} as const satisfies FirewallConfig;",
    "",
  ];
  return lines.join("\n");
}

export async function generate(): Promise<void> {
  console.error("Generating fal.ai firewall config...");
  const ts = generateTypeScript();
  writeOutput("fal", ts, import.meta.dirname);
}
