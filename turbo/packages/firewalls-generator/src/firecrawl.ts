/**
 * Generate Firecrawl firewall config.
 *
 * Data source: https://docs.firecrawl.dev/api-reference/introduction
 *
 * Firecrawl uses Bearer token authentication via Authorization header.
 * Token format: fc- prefix + 32 alphanumeric chars (UUID without dashes).
 */

import { writeOutput } from "./codegen";

const DOCS_URL = "https://docs.firecrawl.dev/api-reference/introduction";
// Format: fc- + 32 alphanumeric chars (UUID without dashes)
const PLACEHOLDER_VALUE = "fc-c0ffee5afe10ca1c0ffee5afe10ca1c0";

function generateTypeScript(): string {
  const lines: string[] = [
    "// Auto-generated from Firecrawl API docs.",
    `// Source: ${DOCS_URL}`,
    "// Regenerate: cd turbo && pnpm -F @vm0/firewalls-generator generate:firecrawl",
    "//",
    "// DO NOT EDIT THIS FILE MANUALLY.",
    "",
    'import type { FirewallConfig } from "../contracts/firewalls";',
    "",
    "export const firecrawlFirewall = {",
    '  name: "firecrawl",',
    '  description: "Firecrawl API",',
    "  placeholders: {",
    `    FIRECRAWL_TOKEN: "${PLACEHOLDER_VALUE}",`,
    "  },",
    "  apis: [",
    "    {",
    '      base: "https://api.firecrawl.dev",',
    "      auth: {",
    "        headers: {",
    '          Authorization: "Bearer ${{ secrets.FIRECRAWL_TOKEN }}",',
    "        },",
    "      },",
    "      permissions: [],",
    "    },",
    "  ],",
    "} as const satisfies FirewallConfig;",
    "",
  ];

  return lines.join("\n");
}

export async function generate(): Promise<void> {
  console.error("Generating Firecrawl firewall config...");
  const ts = generateTypeScript();
  writeOutput("firecrawl", ts, import.meta.dirname);
}
