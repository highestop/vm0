/**
 * Generate Freshdesk firewall config.
 *
 * Data source: https://developers.freshdesk.com/api/
 *
 * Freshdesk is a customer support and helpdesk platform by Freshworks.
 * Base URL is subdomain-based: https://{subdomain}.freshdesk.com
 * The subdomain is provided as a variable (${{ vars.FRESHDESK_DOMAIN }}).
 *
 * Authentication is HTTP Basic with the API key as the username and
 * an empty password. Freshdesk ignores the password field when an API
 * key is supplied — the documented examples use a dummy "X", but an
 * empty password is equally valid and matches our `basic()` template
 * shape (literal strings are not supported).
 */

import { writeOutput } from "./codegen";

const DOCS_URL = "https://developers.freshdesk.com/api/";
// Freshdesk API keys are 20-char opaque alphanumeric strings
// (example from official docs: "abcdefghij1234567890").
const PLACEHOLDER_VALUE = "CoffeeSafeLocalCoffe";

function generateTypeScript(): string {
  const lines: string[] = [
    "// Auto-generated from Freshdesk API docs.",
    `// Source: ${DOCS_URL}`,
    "// Regenerate: cd turbo && pnpm -F @vm0/firewalls-generator generate:freshdesk",
    "//",
    "// DO NOT EDIT THIS FILE MANUALLY.",
    "",
    'import type { FirewallConfig } from "../contracts/firewalls";',
    "",
    "export const freshdeskFirewall = {",
    '  name: "freshdesk",',
    '  description: "Freshdesk helpdesk and customer support API",',
    "  placeholders: {",
    `    FRESHDESK_TOKEN: "${PLACEHOLDER_VALUE}",`,
    "  },",
    "  apis: [",
    "    {",
    '      base: "https://${{ vars.FRESHDESK_DOMAIN }}.freshdesk.com",',
    "      auth: {",
    "        headers: {",
    '          Authorization: "${{ basic(secrets.FRESHDESK_TOKEN, ) }}",',
    "        },",
    "      },",
    "      permissions: [],",
    "    },",
    "  ],",
    "} as const satisfies FirewallConfig;",
    "",
  ];

  return lines.join("\n");
}

export async function generate(): Promise<void> {
  console.error("Generating Freshdesk firewall config...");
  const ts = generateTypeScript();
  writeOutput("freshdesk", ts, import.meta.dirname);
}
