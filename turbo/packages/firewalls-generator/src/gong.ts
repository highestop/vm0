/**
 * Generate Gong firewall config.
 *
 * Data source: https://gong.app.gong.io/settings/api/documentation
 *
 * Gong (API token auth) uses HTTP Basic Auth: the username is the
 * Access Key and the password is the Access Key Secret. The API host
 * is tenant-specific (standard tenants use api.gong.io; regional
 * tenants have their own host), so it is supplied as a variable.
 */

import { writeOutput } from "./codegen";

const DOCS_URL = "https://gong.app.gong.io/settings/api/documentation";
// Gong Access Keys are ~30-char base62 strings; the secret is a long token.
const ACCESS_KEY_PLACEHOLDER = "CoffeeSafeLocalCoffeeSafeLocalCo";
const ACCESS_KEY_SECRET_PLACEHOLDER =
  "CoffeeSafeLocalCoffeeSafeLocalCoffeeSafeLocalCoffeeSafeLocalCoffeeSafeLoca";

function generateTypeScript(): string {
  const lines: string[] = [
    "// Auto-generated from Gong API docs.",
    `// Source: ${DOCS_URL}`,
    "// Regenerate: cd turbo && pnpm -F @vm0/firewalls-generator generate:gong",
    "//",
    "// DO NOT EDIT THIS FILE MANUALLY.",
    "",
    'import type { FirewallConfig } from "../firewall-types";',
    "",
    "export const gongFirewall = {",
    '  name: "gong",',
    '  description: "Gong API (Access Key auth)",',
    "  placeholders: {",
    `    GONG_ACCESS_KEY: "${ACCESS_KEY_PLACEHOLDER}",`,
    `    GONG_ACCESS_KEY_SECRET: "${ACCESS_KEY_SECRET_PLACEHOLDER}",`,
    "  },",
    "  apis: [",
    "    {",
    '      base: "https://${{ vars.GONG_API_BASE }}",',
    "      auth: {",
    "        headers: {",
    "          Authorization:",
    '            "${{ basic(secrets.GONG_ACCESS_KEY, secrets.GONG_ACCESS_KEY_SECRET) }}",',
    "        },",
    "      },",
    "      permissions: [],",
    "    },",
    "  ],",
    "} as const satisfies FirewallConfig;",
    "",
  ];

  return lines.join("\n");
}

export async function generate(): Promise<void> {
  console.error("Generating Gong firewall config...");
  const ts = generateTypeScript();
  writeOutput("gong", ts, import.meta.dirname);
}
