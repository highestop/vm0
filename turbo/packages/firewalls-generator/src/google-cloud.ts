import { writeOutput } from "./codegen";

const DOCS_URL =
  "https://docs.cloud.google.com/sdk/docs/downloads-versioned-archives";
const PLACEHOLDER_VALUE =
  "ya29.A0CoffeeSafeLocalCoffeeSafeLocalCoffeeSafeLocalCoffeeSafeLocalCoffeeSafeLocalCoffeeSafeLocalCoffeeSafeLocalCoffeeSafeLocalCoffeeSafeLocalCoffeeSafeLocalCoffeeSa";

const ENDPOINTS = [
  "https://cloudresourcemanager.googleapis.com",
  "https://serviceusage.googleapis.com",
  "https://iam.googleapis.com",
  "https://compute.googleapis.com",
  "https://appengine.googleapis.com",
  "https://sqladmin.googleapis.com",
  "https://bigquery.googleapis.com",
  "https://storage.googleapis.com",
  "https://run.googleapis.com",
  "https://cloudbuild.googleapis.com",
  "https://artifactregistry.googleapis.com",
  "https://container.googleapis.com",
  "https://cloudfunctions.googleapis.com",
  "https://secretmanager.googleapis.com",
  "https://logging.googleapis.com",
  "https://monitoring.googleapis.com",
  "https://cloudbilling.googleapis.com",
  "https://pubsub.googleapis.com",
  "https://firestore.googleapis.com",
  "https://spanner.googleapis.com",
] as const;

function generateTypeScript(): string {
  const lines: string[] = [
    "// Auto-generated from a curated Google Cloud API host list.",
    `// Source reference: ${DOCS_URL}`,
    "// Regenerate: cd turbo && pnpm -F @vm0/firewalls-generator generate:google-cloud",
    "//",
    "// DO NOT EDIT THIS FILE MANUALLY.",
    "",
    'import type { FirewallConfig } from "../firewall-types";',
    "",
    "export const googleCloudFirewall = {",
    '  name: "google-cloud",',
    '  description: "Google Cloud APIs",',
    "  placeholders: {",
    `    GOOGLE_CLOUD_TOKEN: "${PLACEHOLDER_VALUE}",`,
    "  },",
    "  apis: [",
  ];

  for (const endpoint of ENDPOINTS) {
    lines.push("    {");
    lines.push(`      base: "${endpoint}",`);
    lines.push("      auth: {");
    lines.push("        headers: {");
    lines.push(
      '          Authorization: "Bearer ${{ secrets.GOOGLE_CLOUD_TOKEN }}",',
    );
    lines.push("        },");
    lines.push("      },");
    lines.push("      permissions: [],");
    lines.push("    },");
  }

  lines.push("  ],");
  lines.push("} as const satisfies FirewallConfig;");
  lines.push("");

  return lines.join("\n");
}

export async function generate(): Promise<void> {
  console.error("Generating Google Cloud firewall config...");
  const ts = generateTypeScript();
  writeOutput("google-cloud", ts, import.meta.dirname);
}
