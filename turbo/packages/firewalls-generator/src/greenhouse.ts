/**
 * Generate Greenhouse Harvest firewall config.
 *
 * Data source: https://developers.greenhouse.io/harvest.html
 *
 * Greenhouse Harvest v1/v2 uses HTTP Basic Auth with the API token as the
 * username and an empty password. The token is opaque (no documented prefix
 * or fixed length), so the placeholder uses a generic base62 filler.
 *
 * Note: Harvest v1/v2 will be deprecated on August 31, 2026 — this connector
 * should migrate to Harvest v3 (OAuth) before that date.
 */

import { writeOutput } from "./codegen";

const DOCS_URL = "https://developers.greenhouse.io/harvest.html";
// Greenhouse API key format is opaque; use a generic base62 placeholder.
const PLACEHOLDER_VALUE = "CoffeeSafeLocalCoffeeSafeLocalCoffeeSa";

function generateTypeScript(): string {
  const lines: string[] = [
    "// Auto-generated from Greenhouse Harvest API docs.",
    `// Source: ${DOCS_URL}`,
    "// Regenerate: cd turbo && pnpm -F @vm0/firewalls-generator generate:greenhouse",
    "//",
    "// DO NOT EDIT THIS FILE MANUALLY.",
    "",
    'import type { FirewallConfig } from "../firewall-types";',
    "",
    "export const greenhouseFirewall = {",
    '  name: "greenhouse",',
    '  description: "Greenhouse Harvest API",',
    "  placeholders: {",
    `    GREENHOUSE_TOKEN: "${PLACEHOLDER_VALUE}",`,
    "  },",
    "  apis: [",
    "    {",
    '      base: "https://harvest.greenhouse.io",',
    "      auth: {",
    "        headers: {",
    '          Authorization: "${{ basic(secrets.GREENHOUSE_TOKEN, ) }}",',
    "        },",
    "      },",
    "      permissions: [],",
    "    },",
    "  ],",
    "} as const satisfies FirewallConfig;",
    "",
  ];

  return lines.join("\n");
}

export async function generate(): Promise<void> {
  console.error("Generating Greenhouse firewall config...");
  const ts = generateTypeScript();
  writeOutput("greenhouse", ts, import.meta.dirname);
}
