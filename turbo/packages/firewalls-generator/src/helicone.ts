/**
 * Generate Helicone firewall config.
 *
 * Data source: https://docs.helicone.ai/rest/request/get-v1requests
 *
 * Helicone is an LLM observability platform for logging, cost tracking,
 * and performance analytics. Uses Bearer token authentication.
 */

import { writeOutput } from "./codegen";

const DOCS_URL = "https://docs.helicone.ai/rest/request/get-v1requests";
// Token format: sk-helicone-<uuid> (UUID uses hex digits)
const PLACEHOLDER_VALUE = "sk-helicone-c0ffee5a-fe10-ca1c-0ffe-e5afe10ca1c0";

function generateTypeScript(): string {
  const lines: string[] = [
    "// Auto-generated from Helicone API docs.",
    `// Source: ${DOCS_URL}`,
    "// Regenerate: cd turbo && pnpm -F @vm0/firewalls-generator generate:helicone",
    "//",
    "// DO NOT EDIT THIS FILE MANUALLY.",
    "",
    'import type { FirewallConfig } from "../contracts/firewalls";',
    "",
    "export const heliconeFirewall = {",
    '  name: "helicone",',
    '  description: "Helicone",',
    "  placeholders: {",
    `    HELICONE_TOKEN: "${PLACEHOLDER_VALUE}",`,
    "  },",
    "  apis: [",
    "    {",
    '      base: "https://api.helicone.ai",',
    "      auth: {",
    "        headers: {",
    '          Authorization: "Bearer ${{ secrets.HELICONE_TOKEN }}",',
    "        },",
    "      },",
    "      permissions: [],",
    "    },",
    "  ],",
    "} as const satisfies FirewallConfig;",
    "",
  ];

  return lines.join("\n");
}

export async function generate(): Promise<void> {
  console.error("Generating Helicone firewall config...");
  const ts = generateTypeScript();
  writeOutput("helicone", ts, import.meta.dirname);
}
