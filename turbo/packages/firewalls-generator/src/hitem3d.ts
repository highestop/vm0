/**
 * Generate Hitem3D firewall config.
 *
 * Data sources:
 * - https://docs.hitem3d.ai/en/api/api-reference/list/get-token
 * - https://docs.hitem3d.ai/en/mcp-tools
 *
 * Hitem3D OpenAPI uses Basic Auth with client_id:client_secret to obtain
 * an access token. The Hitem3D MCP endpoint also accepts the same Basic Auth
 * header and performs OpenAPI calls behind the MCP server.
 */

import { writeOutput } from "./codegen";

const API_DOCS_URL =
  "https://docs.hitem3d.ai/en/api/api-reference/list/get-token";
const MCP_DOCS_URL = "https://docs.hitem3d.ai/en/mcp-tools";
const CLIENT_ID_PLACEHOLDER =
  "hitem3d_client_id_CoffeeSafeLocalCoffeeSafeLocal";
const CLIENT_SECRET_PLACEHOLDER =
  "hitem3d_client_secret_CoffeeSafeLocalCoffeeSafeLocal";

function generateTypeScript(): string {
  const lines: string[] = [
    "// Auto-generated from Hitem3D API docs.",
    `// Sources: ${API_DOCS_URL}, ${MCP_DOCS_URL}`,
    "// Regenerate: cd turbo && pnpm -F @vm0/firewalls-generator generate:hitem3d",
    "//",
    "// DO NOT EDIT THIS FILE MANUALLY.",
    "",
    'import type { FirewallConfig } from "../firewall-types";',
    "",
    "export const hitem3dFirewall = {",
    '  name: "hitem3d",',
    '  description: "Hitem3D API",',
    "  placeholders: {",
    `    HITEM3D_CLIENT_ID: "${CLIENT_ID_PLACEHOLDER}",`,
    `    HITEM3D_CLIENT_SECRET: "${CLIENT_SECRET_PLACEHOLDER}",`,
    "  },",
    "  apis: [",
    "    {",
    '      base: "https://api.hitem3d.ai/open-api/v1/auth/token",',
    "      auth: {",
    "        headers: {",
    '          Authorization: "${{ basic(secrets.HITEM3D_CLIENT_ID, secrets.HITEM3D_CLIENT_SECRET) }}",',
    "        },",
    "      },",
    "      permissions: [",
    "        {",
    '          name: "auth-token",',
    '          description: "Obtain a Hitem3D API access token",',
    '          rules: ["POST /"],',
    "        },",
    "      ],",
    "    },",
    "    {",
    '      base: "https://mcp.hitem3d.ai/mcp",',
    "      auth: {",
    "        headers: {",
    '          Authorization: "${{ basic(secrets.HITEM3D_CLIENT_ID, secrets.HITEM3D_CLIENT_SECRET) }}",',
    "        },",
    "      },",
    "      permissions: [],",
    "    },",
    "  ],",
    "} as const satisfies FirewallConfig;",
    "",
  ];

  return lines.join("\n");
}

export async function generate(): Promise<void> {
  console.error("Generating Hitem3D firewall config...");
  const ts = generateTypeScript();
  writeOutput("hitem3d", ts, import.meta.dirname);
}
