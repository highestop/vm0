/**
 * Generate HubSpot firewall config.
 *
 * Data source: https://developers.hubspot.com/docs/api-reference/auth-oauth-v1/guide
 *
 * HubSpot uses Bearer token authentication via Authorization header.
 * OAuth access tokens are base64 strings up to 512 chars.
 * gitleaks hubspot-api-key rule matches legacy UUID API keys, not OAuth tokens.
 * Using generic alphanumeric placeholder for OAuth token.
 */

import { writeOutput } from "./codegen";

const DOCS_URL =
  "https://developers.hubspot.com/docs/api-reference/auth-oauth-v1/guide";
// OAuth access token: base64 string, up to 512 chars
// No fixed prefix; generic alphanumeric placeholder
const PLACEHOLDER_VALUE = "CoffeeSafeLocalCoffeeSafeLocalCo";

function generateTypeScript(): string {
  const lines: string[] = [
    "// Auto-generated from HubSpot API docs.",
    `// Source: ${DOCS_URL}`,
    "// Regenerate: cd turbo && pnpm -F @vm0/firewalls-generator generate:hubspot",
    "//",
    "// DO NOT EDIT THIS FILE MANUALLY.",
    "",
    'import type { FirewallConfig } from "../contracts/firewalls";',
    "",
    "export const hubspotFirewall: FirewallConfig = {",
    '  name: "hubspot",',
    '  description: "HubSpot API",',
    "  placeholders: {",
    `    HUBSPOT_TOKEN: "${PLACEHOLDER_VALUE}",`,
    "  },",
    "  apis: [",
    "    {",
    '      base: "https://api.hubapi.com",',
    "      auth: {",
    "        headers: {",
    '          Authorization: "Bearer ${{ secrets.HUBSPOT_TOKEN }}",',
    "        },",
    "      },",
    "      permissions: [],",
    "    },",
    "  ],",
    "};",
    "",
  ];

  return lines.join("\n");
}

export async function generate(): Promise<void> {
  console.error("Generating HubSpot firewall config...");
  const ts = generateTypeScript();
  writeOutput("hubspot", ts, import.meta.dirname);
}
