/**
 * Generate Hugging Face firewall config.
 *
 * Data source: https://huggingface.co/docs/hub/security-tokens
 *
 * Hugging Face is an AI/ML model hosting and inference platform.
 * Uses Bearer token authentication.
 * Token format: hf_ prefix + alphanumeric, ~37 chars total.
 * Covers both the Hub API and the Inference API.
 */

import { writeOutput } from "./codegen";

const DOCS_URL = "https://huggingface.co/docs/hub/security-tokens";
// Token format: hf_ + alphanumeric, ~34 chars after prefix
const PLACEHOLDER_VALUE = "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";

function generateTypeScript(): string {
  const lines: string[] = [
    "// Auto-generated from Hugging Face API docs.",
    `// Source: ${DOCS_URL}`,
    "// Regenerate: cd turbo && pnpm -F @vm0/firewalls-generator generate:hugging-face",
    "//",
    "// DO NOT EDIT THIS FILE MANUALLY.",
    "",
    'import type { FirewallConfig } from "../contracts/firewalls";',
    "",
    "export const huggingFaceFirewall = {",
    '  name: "hugging-face",',
    '  description: "Hugging Face API",',
    "  placeholders: {",
    `    HUGGING_FACE_TOKEN: "${PLACEHOLDER_VALUE}",`,
    "  },",
    "  apis: [",
    "    {",
    '      base: "https://huggingface.co/api",',
    "      auth: {",
    "        headers: {",
    '          Authorization: "Bearer ${{ secrets.HUGGING_FACE_TOKEN }}",',
    "        },",
    "      },",
    "      permissions: [],",
    "    },",
    "    {",
    '      base: "https://api-inference.huggingface.co",',
    "      auth: {",
    "        headers: {",
    '          Authorization: "Bearer ${{ secrets.HUGGING_FACE_TOKEN }}",',
    "        },",
    "      },",
    "      permissions: [],",
    "    },",
    "    {",
    '      base: "https://router.huggingface.co",',
    "      auth: {",
    "        headers: {",
    '          Authorization: "Bearer ${{ secrets.HUGGING_FACE_TOKEN }}",',
    "        },",
    "      },",
    "      permissions: [],",
    "    },",
    "  ],",
    "} as const satisfies FirewallConfig;",
    "",
  ];

  return lines.join("\n");
}

export async function generate(): Promise<void> {
  console.error("Generating Hugging Face firewall config...");
  const ts = generateTypeScript();
  writeOutput("hugging-face", ts, import.meta.dirname);
}
