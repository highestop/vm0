/**
 * Generate Imgur firewall config.
 *
 * Data source: https://apidocs.imgur.com/
 *
 * Imgur is an image hosting and sharing platform.
 * Uses Client-ID authentication (not Bearer).
 * Auth header: Authorization: Client-ID <client_id>
 * Token format: opaque string, no known prefix.
 * Only unrestricted permissions for now.
 */

import { writeOutput } from "./codegen";

const DOCS_URL = "https://apidocs.imgur.com/";
// Client ID format not documented; using generic placeholder
const PLACEHOLDER_VALUE = "vm0placeholder000000000000000000a";

function generateTypeScript(): string {
  const lines: string[] = [
    "// Auto-generated from Imgur API docs.",
    `// Source: ${DOCS_URL}`,
    "// Regenerate: cd turbo && pnpm -F @vm0/firewalls-generator generate:imgur",
    "//",
    "// DO NOT EDIT THIS FILE MANUALLY.",
    "",
    'import type { FirewallConfig } from "../contracts/firewalls";',
    "",
    "export const imgurFirewall: FirewallConfig = {",
    '  name: "imgur",',
    '  description: "Imgur API",',
    "  placeholders: {",
    `    IMGUR_CLIENT_ID: "${PLACEHOLDER_VALUE}",`,
    "  },",
    "  apis: [",
    "    {",
    '      base: "https://api.imgur.com",',
    "      auth: {",
    "        headers: {",
    '          Authorization: "Client-ID ${{ secrets.IMGUR_CLIENT_ID }}",',
    "        },",
    "      },",
    "      permissions: [",
    "        {",
    '          name: "unrestricted",',
    '          description: "Allow all endpoints",',
    '          rules: ["ANY /{path*}"],',
    "        },",
    "      ],",
    "    },",
    "  ],",
    "};",
    "",
  ];

  return lines.join("\n");
}

export async function generate(): Promise<void> {
  console.error("Generating Imgur firewall config...");
  const ts = generateTypeScript();
  writeOutput("imgur", ts, import.meta.dirname);
}
