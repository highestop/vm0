/**
 * Generate Jira firewall config.
 *
 * Data source: https://developer.atlassian.com/cloud/jira/platform/rest/v3/
 *
 * Jira (API token auth) uses HTTP Basic Auth with the user's email as
 * the username and an API token as the password. The base URL includes
 * the customer's Atlassian domain.
 */

import { writeOutput } from "./codegen";

const DOCS_URL = "https://developer.atlassian.com/cloud/jira/platform/rest/v3/";
// Jira API tokens are ~24-char alphanumeric strings
const PLACEHOLDER_VALUE = "Coffee5afe10ca1Coffee5af";

function generateTypeScript(): string {
  const lines: string[] = [
    "// Auto-generated from Jira REST API v3 docs.",
    `// Source: ${DOCS_URL}`,
    "// Regenerate: cd turbo && pnpm -F @vm0/firewalls-generator generate:jira",
    "//",
    "// DO NOT EDIT THIS FILE MANUALLY.",
    "",
    'import type { FirewallConfig } from "../contracts/firewalls";',
    "",
    "export const jiraFirewall = {",
    '  name: "jira",',
    '  description: "Jira REST API (API token auth)",',
    "  placeholders: {",
    `    JIRA_API_TOKEN: "${PLACEHOLDER_VALUE}",`,
    "  },",
    "  apis: [",
    "    {",
    '      base: "https://${{ vars.JIRA_DOMAIN }}",',
    "      auth: {",
    "        headers: {",
    '          Authorization: "${{ basic(vars.JIRA_EMAIL, secrets.JIRA_API_TOKEN) }}",',
    "        },",
    "      },",
    "      permissions: [],",
    "    },",
    "  ],",
    "} as const satisfies FirewallConfig;",
    "",
  ];

  return lines.join("\n");
}

export async function generate(): Promise<void> {
  console.error("Generating Jira firewall config...");
  const ts = generateTypeScript();
  writeOutput("jira", ts, import.meta.dirname);
}
