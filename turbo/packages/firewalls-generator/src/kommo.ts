/**
 * Generate Kommo firewall config.
 *
 * Data source: https://www.kommo.com/developers/content/api/
 *
 * Kommo is a CRM for managing leads, contacts, and sales pipelines.
 * Base URL is subdomain-based: https://{subdomain}.kommo.com
 * The subdomain is provided as a variable (${{ vars.KOMMO_SUBDOMAIN }}).
 */

import { writeOutput } from "./codegen";

const DOCS_URL = "https://www.kommo.com/developers/content/api/";
// Format: Kommo API keys are long alphanumeric strings
const PLACEHOLDER_VALUE = "kmApiKey_CoffeeSafeLocalCoffeeSafeLocalCoffee";

function generateTypeScript(): string {
  const lines: string[] = [
    "// Auto-generated from Kommo API docs.",
    `// Source: ${DOCS_URL}`,
    "// Regenerate: cd turbo && pnpm -F @vm0/firewalls-generator generate:kommo",
    "//",
    "// DO NOT EDIT THIS FILE MANUALLY.",
    "",
    'import type { FirewallConfig } from "../contracts/firewalls";',
    "",
    "export const kommoFirewall = {",
    '  name: "kommo",',
    '  description: "Kommo CRM API",',
    "  placeholders: {",
    `    KOMMO_API_KEY: "${PLACEHOLDER_VALUE}",`,
    "  },",
    "  apis: [",
    "    {",
    '      base: "https://${{ vars.KOMMO_SUBDOMAIN }}.kommo.com",',
    "      auth: {",
    "        headers: {",
    '          Authorization: "Bearer ${{ secrets.KOMMO_API_KEY }}",',
    "        },",
    "      },",
    "      permissions: [],",
    "    },",
    "  ],",
    "} as const satisfies FirewallConfig;",
    "",
  ];

  return lines.join("\n");
}

export async function generate(): Promise<void> {
  console.error("Generating Kommo firewall config...");
  const ts = generateTypeScript();
  writeOutput("kommo", ts, import.meta.dirname);
}
