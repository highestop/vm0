/**
 * Generate Langfuse firewall config.
 *
 * Data source: https://langfuse.com/docs/api
 *
 * Langfuse uses HTTP Basic Auth with the project's Public Key as the
 * username and Secret Key as the password for all API endpoints.
 *
 * Both keys are UUID-based strings with a short prefix:
 *   Public Key:  pk-lf-<uuid>  (e.g. pk-lf-xxxxxxxx-xxxx-xxxx-xxxx-xxxxxxxxxxxx)
 *   Secret Key:  sk-lf-<uuid>  (e.g. sk-lf-xxxxxxxx-xxxx-xxxx-xxxx-xxxxxxxxxxxx)
 *
 * Two regional hosts are supported:
 *   EU Cloud (default): https://cloud.langfuse.com
 *   US Cloud:           https://us.cloud.langfuse.com
 */

import { writeOutput } from "./codegen";

const DOCS_URL = "https://langfuse.com/docs/api";
// UUID-format placeholders built from Coffee/Safe/Local hex vocab.
const PUBLIC_KEY_PLACEHOLDER = "pk-lf-c0ffee5a-fe10-ca1c-0ffe-e5afe10ca1c0";
const SECRET_KEY_PLACEHOLDER = "sk-lf-5afe10ca-1c0f-feec-0ffe-e5afe10ca1c0";

function generateTypeScript(): string {
  const lines: string[] = [
    "// Auto-generated from Langfuse API docs.",
    `// Source: ${DOCS_URL}`,
    "// Regenerate: cd turbo && pnpm -F @vm0/firewalls-generator generate:langfuse",
    "//",
    "// DO NOT EDIT THIS FILE MANUALLY.",
    "",
    'import type { FirewallConfig } from "../firewall-types";',
    "",
    "export const langfuseFirewall = {",
    '  name: "langfuse",',
    '  description: "Langfuse LLM observability and tracing API",',
    "  placeholders: {",
    `    LANGFUSE_PUBLIC_KEY: "${PUBLIC_KEY_PLACEHOLDER}",`,
    `    LANGFUSE_SECRET_KEY: "${SECRET_KEY_PLACEHOLDER}",`,
    "  },",
    "  apis: [",
    "    {",
    '      base: "https://cloud.langfuse.com",',
    "      auth: {",
    "        headers: {",
    "          Authorization:",
    '            "${{ basic(secrets.LANGFUSE_PUBLIC_KEY, secrets.LANGFUSE_SECRET_KEY) }}",',
    "        },",
    "      },",
    "      permissions: [],",
    "    },",
    "    {",
    '      base: "https://us.cloud.langfuse.com",',
    "      auth: {",
    "        headers: {",
    "          Authorization:",
    '            "${{ basic(secrets.LANGFUSE_PUBLIC_KEY, secrets.LANGFUSE_SECRET_KEY) }}",',
    "        },",
    "      },",
    "      permissions: [],",
    "    },",
    "  ],",
    "} as const satisfies FirewallConfig;",
    "",
  ];

  return lines.join("\n");
}

export async function generate(): Promise<void> {
  console.error("Generating Langfuse firewall config...");
  const ts = generateTypeScript();
  writeOutput("langfuse", ts, import.meta.dirname);
}
