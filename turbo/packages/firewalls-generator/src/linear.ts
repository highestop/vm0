/**
 * Generate Linear firewall config.
 *
 * Data source: https://developers.linear.app/docs/graphql/working-with-the-graphql-api
 *
 * Linear is a project management tool for software teams.
 * OAuth access tokens use Bearer prefix in Authorization header.
 * Token format: 64-character hex string.
 */

import { writeOutput } from "./codegen";

const DOCS_URL =
  "https://developers.linear.app/docs/graphql/working-with-the-graphql-api";
// OAuth access token: 64-char hex string
const PLACEHOLDER_VALUE =
  "c0ffee5afe10ca1c0ffee5afe10ca1c0ffee5afe10ca1c0ffee5afe10ca1c0ff";

function generateTypeScript(): string {
  const lines: string[] = [
    "// Auto-generated from Linear API docs.",
    `// Source: ${DOCS_URL}`,
    "// Regenerate: cd turbo && pnpm -F @vm0/firewalls-generator generate:linear",
    "//",
    "// DO NOT EDIT THIS FILE MANUALLY.",
    "",
    'import type { FirewallConfig } from "../firewall-types";',
    "",
    "export const linearFirewall = {",
    '  name: "linear",',
    '  description: "Linear API",',
    "  placeholders: {",
    `    LINEAR_TOKEN: "${PLACEHOLDER_VALUE}",`,
    "  },",
    "  apis: [",
    "    {",
    '      base: "https://api.linear.app",',
    "      auth: {",
    "        headers: {",
    '          Authorization: "Bearer ${{ secrets.LINEAR_TOKEN }}",',
    "        },",
    "      },",
    "      permissions: [],",
    "    },",
    "  ],",
    "} as const satisfies FirewallConfig;",
    "",
  ];

  return lines.join("\n");
}

export async function generate(): Promise<void> {
  console.error("Generating Linear firewall config...");
  const ts = generateTypeScript();
  writeOutput("linear", ts, import.meta.dirname);
}
