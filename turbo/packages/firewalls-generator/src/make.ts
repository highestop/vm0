/**
 * Generate Make firewall config.
 *
 * Data source: https://developers.make.com/api-documentation
 *
 * Make uses regional subdomains with 4 known regions:
 * eu1.make.com, eu2.make.com, us1.make.com, us2.make.com
 * All regions share the same auth pattern.
 */

import { writeOutput } from "./codegen";

const DOCS_URL = "https://developers.make.com/api-documentation";
// Format: Make API tokens are UUIDs or long hex strings
const PLACEHOLDER_VALUE = "mkTkn-CoffeeSafeLoca-lCof-feeS-afeLocalCoff";

const MAKE_DOMAINS = [
  "eu1.make.com",
  "eu2.make.com",
  "us1.make.com",
  "us2.make.com",
  "eu1.make.celonis.com",
  "us1.make.celonis.com",
];

function generateTypeScript(): string {
  const apiEntries = MAKE_DOMAINS.map(
    (domain) => `    {
      base: "https://${domain}",
      auth: {
        headers: {
          Authorization: "Token $\{{ secrets.MAKE_TOKEN }}",
        },
      },
      permissions: [],
    }`,
  ).join(",\n");

  const lines: string[] = [
    "// Auto-generated from Make API docs.",
    `// Source: ${DOCS_URL}`,
    "// Regenerate: cd turbo && pnpm -F @vm0/firewalls-generator generate:make",
    "//",
    "// DO NOT EDIT THIS FILE MANUALLY.",
    "",
    'import type { FirewallConfig } from "../contracts/firewalls";',
    "",
    "export const makeFirewall = {",
    '  name: "make",',
    '  description: "Make (formerly Integromat) Automation API",',
    "  placeholders: {",
    `    MAKE_TOKEN: "${PLACEHOLDER_VALUE}",`,
    "  },",
    "  apis: [",
    apiEntries + ",",
    "  ],",
    "} as const satisfies FirewallConfig;",
    "",
  ];

  return lines.join("\n");
}

export async function generate(): Promise<void> {
  console.error("Generating Make firewall config...");
  const ts = generateTypeScript();
  writeOutput("make", ts, import.meta.dirname);
}
