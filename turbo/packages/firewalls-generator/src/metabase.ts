/**
 * Generate Metabase firewall config.
 *
 * Data source: https://www.metabase.com/docs/latest/api-documentation
 *
 * Metabase uses x-api-key header for authentication.
 * The base URL is instance-specific (cloud or self-hosted).
 * Users must provide a METABASE_BASE_URL variable.
 *
 * API key format: mb_xxxxxxxxxxxxxxxxxxxxxxxxxxxxxxxxxxxxxxxxxxxx (47 chars).
 */

import { writeOutput } from "./codegen";

const DOCS_URL = "https://www.metabase.com/docs/latest/api-documentation";
// Format: mb_ + 44 alphanumeric chars (total 47)
const PLACEHOLDER_VALUE = "mb_CoffeeSafeLocalCoffeeSafeLocalCoffeeSafeLoca";

function generateTypeScript(): string {
  const lines: string[] = [
    "// Auto-generated from Metabase API docs.",
    `// Source: ${DOCS_URL}`,
    "// Regenerate: cd turbo && pnpm -F @vm0/firewalls-generator generate:metabase",
    "//",
    "// DO NOT EDIT THIS FILE MANUALLY.",
    "",
    'import type { FirewallConfig } from "../firewall-types";',
    "",
    "export const metabaseFirewall = {",
    '  name: "metabase",',
    '  description: "Metabase API",',
    "  placeholders: {",
    `    METABASE_TOKEN: "${PLACEHOLDER_VALUE}",`,
    "  },",
    "  apis: [",
    "    {",
    '      base: "${{ vars.METABASE_BASE_URL }}",',
    "      auth: {",
    "        headers: {",
    '          "x-api-key": "${{ secrets.METABASE_TOKEN }}",',
    "        },",
    "      },",
    "      permissions: [],",
    "    },",
    "  ],",
    "} as const satisfies FirewallConfig;",
    "",
  ];

  return lines.join("\n");
}

export async function generate(): Promise<void> {
  console.error("Generating Metabase firewall config...");
  const ts = generateTypeScript();
  writeOutput("metabase", ts, import.meta.dirname);
}
