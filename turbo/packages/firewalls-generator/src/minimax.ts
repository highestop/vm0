/**
 * Generate MiniMax firewall config.
 *
 * Data source: https://platform.minimax.io/docs/guides/quickstart-preparation
 *
 * MiniMax is an AI model provider.
 * Uses Bearer token authentication.
 * Token format: opaque API key string.
 */

import { writeOutput } from "./codegen";

const DOCS_URL =
  "https://platform.minimax.io/docs/guides/quickstart-preparation";
// API key format not documented; using generic placeholder
const PLACEHOLDER_VALUE =
  "eyJhbGciOiJSUzI1NiJ9.CoffeeSafeLocalCoffeeSafeLocalCoffeeSafeLocal";

function generateTypeScript(): string {
  const lines: string[] = [
    "// Auto-generated from MiniMax API docs.",
    `// Source: ${DOCS_URL}`,
    "// Regenerate: cd turbo && pnpm -F @vm0/firewalls-generator generate:minimax",
    "//",
    "// DO NOT EDIT THIS FILE MANUALLY.",
    "",
    'import type { FirewallConfig } from "../contracts/firewalls";',
    "",
    "export const minimaxFirewall = {",
    '  name: "minimax",',
    '  description: "MiniMax API",',
    "  placeholders: {",
    `    MINIMAX_TOKEN: "${PLACEHOLDER_VALUE}",`,
    "  },",
    "  apis: [",
    "    {",
    '      base: "https://api.minimax.io",',
    "      auth: {",
    "        headers: {",
    '          Authorization: "Bearer ${{ secrets.MINIMAX_TOKEN }}",',
    "        },",
    "      },",
    "      permissions: [],",
    "    },",
    "  ],",
    "} as const satisfies FirewallConfig;",
    "",
  ];

  return lines.join("\n");
}

export async function generate(): Promise<void> {
  console.error("Generating MiniMax firewall config...");
  const ts = generateTypeScript();
  writeOutput("minimax", ts, import.meta.dirname);
}
