/**
 * Generate Mixpanel firewall config.
 *
 * Data source: https://developer.mixpanel.com/reference/authentication
 *
 * Mixpanel uses HTTP Basic auth with Service Account credentials — the
 * username is the service account slug and the password is the secret.
 * Every request also requires a `project_id` query parameter, which the
 * skill supplies at call time.
 *
 * Three hosts are reachable depending on the surface:
 *   - mixpanel.com        — Insights, Funnels, Segmentation, JQL, Engage
 *   - data.mixpanel.com   — Raw event export
 *   - api.mixpanel.com    — Ingestion (/import, /track, /engage)
 */

import { writeOutput } from "./codegen";

const DOCS_URL = "https://developer.mixpanel.com/reference/authentication";
// Service Account username: ~20-char base62 slug.
const USERNAME_PLACEHOLDER = "CoffeeSafeLocalCoffe";
// Service Account secret: 32-char base62 opaque token.
const SECRET_PLACEHOLDER = "CoffeeSafeLocalCoffeeSafeLocalCo";

function generateTypeScript(): string {
  const lines: string[] = [
    "// Auto-generated from Mixpanel API docs.",
    `// Source: ${DOCS_URL}`,
    "// Regenerate: cd turbo && pnpm -F @vm0/firewalls-generator generate:mixpanel",
    "//",
    "// DO NOT EDIT THIS FILE MANUALLY.",
    "",
    'import type { FirewallConfig } from "../contracts/firewalls";',
    "",
    "export const mixpanelFirewall = {",
    '  name: "mixpanel",',
    '  description: "Mixpanel product analytics API",',
    "  placeholders: {",
    `    MIXPANEL_SERVICE_ACCOUNT_USERNAME: "${USERNAME_PLACEHOLDER}",`,
    `    MIXPANEL_SERVICE_ACCOUNT_SECRET: "${SECRET_PLACEHOLDER}",`,
    "  },",
    "  apis: [",
    "    {",
    '      base: "https://mixpanel.com",',
    "      auth: {",
    "        headers: {",
    "          Authorization:",
    '            "${{ basic(secrets.MIXPANEL_SERVICE_ACCOUNT_USERNAME, secrets.MIXPANEL_SERVICE_ACCOUNT_SECRET) }}",',
    "        },",
    "      },",
    "      permissions: [],",
    "    },",
    "    {",
    '      base: "https://data.mixpanel.com",',
    "      auth: {",
    "        headers: {",
    "          Authorization:",
    '            "${{ basic(secrets.MIXPANEL_SERVICE_ACCOUNT_USERNAME, secrets.MIXPANEL_SERVICE_ACCOUNT_SECRET) }}",',
    "        },",
    "      },",
    "      permissions: [],",
    "    },",
    "    {",
    '      base: "https://api.mixpanel.com",',
    "      auth: {",
    "        headers: {",
    "          Authorization:",
    '            "${{ basic(secrets.MIXPANEL_SERVICE_ACCOUNT_USERNAME, secrets.MIXPANEL_SERVICE_ACCOUNT_SECRET) }}",',
    "        },",
    "      },",
    "      permissions: [],",
    "    },",
    "  ],",
    "} as const satisfies FirewallConfig;",
    "",
  ];

  return lines.join("\n");
}

export async function generate(): Promise<void> {
  console.error("Generating Mixpanel firewall config...");
  const ts = generateTypeScript();
  writeOutput("mixpanel", ts, import.meta.dirname);
}
