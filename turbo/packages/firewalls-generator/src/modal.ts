/**
 * Generate Modal firewall config.
 *
 * Data sources:
 * - https://modal.com/docs/reference/modal.config
 * - https://github.com/modal-labs/modal-client
 *
 * Modal's SDK reads MODAL_TOKEN_ID and MODAL_TOKEN_SECRET from the
 * environment, then sends them as gRPC metadata headers to api.modal.com.
 */

import { writeOutput } from "./codegen";

const DOCS_URL = "https://modal.com/docs/reference/modal.config";
const TOKEN_ID_PLACEHOLDER = "ak-CoffeeSafeLocalCoffeeSafeLocalCoffee";
const TOKEN_SECRET_PLACEHOLDER = "as-CoffeeSafeLocalCoffeeSafeLocalCoffee";

function generateTypeScript(): string {
  const lines: string[] = [
    "// Auto-generated from Modal SDK auth docs.",
    `// Source: ${DOCS_URL}`,
    "// Regenerate: cd turbo && pnpm -F @vm0/firewalls-generator generate:modal",
    "//",
    "// DO NOT EDIT THIS FILE MANUALLY.",
    "",
    'import type { FirewallConfig } from "../firewall-types";',
    "",
    "export const modalFirewall = {",
    '  name: "modal",',
    '  description: "Modal API",',
    "  placeholders: {",
    `    MODAL_TOKEN_ID: "${TOKEN_ID_PLACEHOLDER}",`,
    `    MODAL_TOKEN_SECRET: "${TOKEN_SECRET_PLACEHOLDER}",`,
    "  },",
    "  apis: [",
    "    {",
    '      base: "https://api.modal.com",',
    "      auth: {",
    "        headers: {",
    '          "x-modal-token-id": "${{ secrets.MODAL_TOKEN_ID }}",',
    '          "x-modal-token-secret": "${{ secrets.MODAL_TOKEN_SECRET }}",',
    "        },",
    "      },",
    "      permissions: [],",
    "    },",
    "  ],",
    "} as const satisfies FirewallConfig;",
    "",
  ];

  return lines.join("\n");
}

export async function generate(): Promise<void> {
  console.error("Generating Modal firewall config...");
  const ts = generateTypeScript();
  writeOutput("modal", ts, import.meta.dirname);
}
