/**
 * Generate Monday.com firewall config.
 *
 * Data source: https://developer.monday.com/api-reference/docs/authentication
 *
 * Monday.com uses token authentication via Authorization header.
 * OAuth access tokens use Bearer prefix in Authorization header.
 * No documented token format; using generic alphanumeric placeholder.
 */

import { writeOutput } from "./codegen";

const DOCS_URL =
  "https://developer.monday.com/api-reference/docs/authentication";
// No documented format; generic 32-char alphanumeric
const PLACEHOLDER_VALUE = "Vm0PlaceHolder00000000000000000a";

function generateTypeScript(): string {
  const lines: string[] = [
    "// Auto-generated from Monday.com API docs.",
    `// Source: ${DOCS_URL}`,
    "// Regenerate: cd turbo && pnpm -F @vm0/firewalls-generator generate:monday",
    "//",
    "// DO NOT EDIT THIS FILE MANUALLY.",
    "",
    'import type { FirewallConfig } from "../contracts/firewalls";',
    "",
    "export const mondayFirewall: FirewallConfig = {",
    '  name: "monday",',
    '  description: "Monday.com API",',
    "  placeholders: {",
    `    MONDAY_TOKEN: "${PLACEHOLDER_VALUE}",`,
    "  },",
    "  apis: [",
    "    {",
    '      base: "https://api.monday.com",',
    "      auth: {",
    "        headers: {",
    '          Authorization: "Bearer ${{ secrets.MONDAY_TOKEN }}",',
    "        },",
    "      },",
    "      permissions: [",
    "        {",
    '          name: "unrestricted",',
    '          description: "Allow all endpoints",',
    '          rules: ["ANY /{path*}"],',
    "        },",
    "      ],",
    "    },",
    "  ],",
    "};",
    "",
  ];

  return lines.join("\n");
}

export async function generate(): Promise<void> {
  console.error("Generating Monday.com firewall config...");
  const ts = generateTypeScript();
  writeOutput("monday", ts, import.meta.dirname);
}
