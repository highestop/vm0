/**
 * Generate Moss firewall config.
 *
 * Moss authenticates every API call with a pair of project credentials:
 * `MOSS_PROJECT_ID` (sent as `X-Moss-Project-Id`) and `MOSS_PROJECT_KEY`
 * (sent as `Authorization: Bearer …`). Both values are scoped to a single
 * Moss project and rotate together.
 */

import { writeOutput } from "./codegen";

const DOCS_URL = "https://docs.moss.dev/docs/start/quickstart";
const PROJECT_ID_PLACEHOLDER = "prj_CoffeeSafeLocalCoffeeSafe";
const PROJECT_KEY_PLACEHOLDER = "msk_CoffeeSafeLocalCoffeeSafeLocalCoffeeSafe";

function generateTypeScript(): string {
  const lines: string[] = [
    "// Auto-generated from Moss API docs.",
    `// Source: ${DOCS_URL}`,
    "// Regenerate: cd turbo && pnpm -F @vm0/firewalls-generator generate:moss",
    "//",
    "// DO NOT EDIT THIS FILE MANUALLY.",
    "",
    'import type { FirewallConfig } from "../firewall-types";',
    "",
    "export const mossFirewall = {",
    '  name: "moss",',
    '  description: "Moss real-time semantic search",',
    "  placeholders: {",
    `    MOSS_PROJECT_ID: "${PROJECT_ID_PLACEHOLDER}",`,
    `    MOSS_PROJECT_KEY: "${PROJECT_KEY_PLACEHOLDER}",`,
    "  },",
    "  apis: [",
    "    {",
    '      base: "https://api.moss.dev",',
    "      auth: {",
    "        headers: {",
    '          "X-Moss-Project-Id": "${{ secrets.MOSS_PROJECT_ID }}",',
    '          Authorization: "Bearer ${{ secrets.MOSS_PROJECT_KEY }}",',
    "        },",
    "      },",
    "      permissions: [],",
    "    },",
    "  ],",
    "} as const satisfies FirewallConfig;",
    "",
  ];
  return lines.join("\n");
}

export async function generate(): Promise<void> {
  console.error("Generating Moss firewall config...");
  const ts = generateTypeScript();
  writeOutput("moss", ts, import.meta.dirname);
}
