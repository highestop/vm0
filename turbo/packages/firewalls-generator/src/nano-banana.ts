import { writeOutput } from "./codegen";

const DOCS_URL = "https://ai.google.dev/gemini-api/docs/image-generation";
const PLACEHOLDER_VALUE =
  "CoffeeSafeLocalCoffeeSafeLocalCoffeeSafeLocalCoffeeSaf";

const MODELS: readonly string[] = [
  "gemini-3-pro-image-preview",
  "gemini-3.1-flash-image-preview",
  "gemini-2.5-flash-image",
];

function renderApiEntry(model: string): string {
  const base = `https://generativelanguage.googleapis.com/v1beta/models/${model}:generateContent`;
  return [
    "    {",
    `      base: "${base}",`,
    "      auth: {",
    "        headers: {",
    '          "x-goog-api-key": "${{ secrets.NANO_BANANA_TOKEN }}",',
    "        },",
    "      },",
    "      permissions: [],",
    "    },",
  ].join("\n");
}

function generateTypeScript(): string {
  const header = [
    "// Auto-generated - Nano Banana (Google Gemini image generation) firewall.",
    `// Source: ${DOCS_URL}`,
    "// Regenerate: cd turbo && pnpm -F @vm0/firewalls-generator generate:nano-banana",
    "//",
    "// DO NOT EDIT THIS FILE MANUALLY.",
    "",
    'import type { FirewallConfig } from "../firewall-types";',
    "",
    "export const nanoBananaFirewall = {",
    '  name: "nano-banana",',
    '  description: "Google Gemini image generation (Nano Banana)",',
    "  placeholders: {",
    `    NANO_BANANA_TOKEN: "${PLACEHOLDER_VALUE}",`,
    "  },",
    "  apis: [",
  ];
  const apis = MODELS.map(renderApiEntry);
  const footer = ["  ],", "} as const satisfies FirewallConfig;", ""];
  return [...header, ...apis, ...footer].join("\n");
}

export async function generate(): Promise<void> {
  console.error("Generating Nano Banana firewall config...");
  writeOutput("nano-banana", generateTypeScript(), import.meta.dirname);
}
