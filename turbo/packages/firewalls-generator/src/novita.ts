/**
 * Generate Novita AI firewall config.
 *
 * Data source: https://novita.ai/docs/api-reference
 *
 * Novita AI uses Bearer token authentication via the Authorization header.
 * A single API host (api.novita.ai) serves the OpenAI-compatible LLM
 * endpoints, the native image/video/audio generation APIs, and GPU
 * management, so a single host entry covers the whole surface.
 * Token format: `sk_`-prefixed base62 string.
 */

import { writeOutput } from "./codegen";

const DOCS_URL = "https://novita.ai/docs/api-reference";
// `sk_`-prefixed placeholder using the Coffee/Safe/Local base62 vocabulary so
// the proxy regex still matches without leaking a real key shape.
const PLACEHOLDER_VALUE = "sk_CoffeeSafeLocalCoffeeSafeLocalCoffeeSafeLoc";

function generateTypeScript(): string {
  const lines: string[] = [
    "// Auto-generated from Novita AI API docs.",
    `// Source: ${DOCS_URL}`,
    "// Regenerate: cd turbo && pnpm -F @vm0/firewalls-generator generate:novita",
    "//",
    "// DO NOT EDIT THIS FILE MANUALLY.",
    "",
    'import type { FirewallConfig } from "../firewall-types";',
    "",
    "export const novitaFirewall = {",
    '  name: "novita",',
    '  description: "Novita AI",',
    "  placeholders: {",
    `    NOVITA_TOKEN: "${PLACEHOLDER_VALUE}",`,
    "  },",
    "  apis: [",
    "    {",
    '      base: "https://api.novita.ai",',
    "      auth: {",
    "        headers: {",
    '          Authorization: "Bearer ${{ secrets.NOVITA_TOKEN }}",',
    "        },",
    "      },",
    "      permissions: [],",
    "    },",
    "  ],",
    "} as const satisfies FirewallConfig;",
    "",
  ];

  return lines.join("\n");
}

export async function generate(): Promise<void> {
  console.error("Generating Novita AI firewall config...");
  const ts = generateTypeScript();
  writeOutput("novita", ts, import.meta.dirname);
}
