/**
 * Generate OpenAI firewall config.
 *
 * Data source: https://platform.openai.com/docs/api-reference/introduction
 *
 * OpenAI uses Bearer token authentication via Authorization header.
 * Token format (gitleaks: openai-api-key):
 *   sk-proj- + 74 chars + T3BlbkFJ + 74 chars (project key, newer format)
 *   sk- + 20 chars + T3BlbkFJ + 20 chars (legacy format)
 */

import { writeOutput } from "./codegen";

const DOCS_URL = "https://platform.openai.com/docs/api-reference/introduction";
// Format: sk-proj- + [A-Za-z0-9_-]{74} + T3BlbkFJ + [A-Za-z0-9_-]{74}
// (gitleaks: openai-api-key)
const PLACEHOLDER_VALUE =
  "sk-proj-CoffeeSafeLocalCoffeeSafeLocalCoffeeSafeLocalCoffeeSafeLocaT3BlbkFJCoffeeSafeLocalCoffeeSafeLocalCoffeeSafeLocalCoffeeSafeLoca";

function generateTypeScript(): string {
  const lines: string[] = [
    "// Auto-generated from OpenAI API docs.",
    `// Source: ${DOCS_URL}`,
    "// Regenerate: cd turbo && pnpm -F @vm0/firewalls-generator generate:openai",
    "//",
    "// DO NOT EDIT THIS FILE MANUALLY.",
    "",
    'import type { FirewallConfig } from "../contracts/firewalls";',
    "",
    "export const openaiFirewall = {",
    '  name: "openai",',
    '  description: "OpenAI API",',
    "  placeholders: {",
    `    OPENAI_TOKEN: "${PLACEHOLDER_VALUE}",`,
    "  },",
    "  apis: [",
    "    {",
    '      base: "https://api.openai.com",',
    "      auth: {",
    "        headers: {",
    '          Authorization: "Bearer ${{ secrets.OPENAI_TOKEN }}",',
    "        },",
    "      },",
    "      permissions: [],",
    "    },",
    "  ],",
    "} as const satisfies FirewallConfig;",
    "",
  ];

  return lines.join("\n");
}

export async function generate(): Promise<void> {
  console.error("Generating OpenAI firewall config...");
  const ts = generateTypeScript();
  writeOutput("openai", ts, import.meta.dirname);
}
