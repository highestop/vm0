/**
 * Generate PandaDoc firewall config.
 *
 * Data source: https://developers.pandadoc.com/reference/about
 * Auth scheme: https://developers.pandadoc.com/reference/api-key-authentication-process
 *
 * PandaDoc uses a CUSTOM Authorization header: `API-Key <token>` (NOT Bearer).
 * Production and Sandbox keys are both opaque alphanumeric strings generated
 * from the app's Settings > Integrations > API page.
 */

import { writeOutput } from "./codegen";

const DOCS_URL = "https://developers.pandadoc.com/reference/about";

// PandaDoc API keys are opaque alphanumeric strings (no documented prefix).
// Real keys are long base62 tokens; use a 64-char CoffeeSafeLocal fill.
const PLACEHOLDER_VALUE =
  "CoffeeSafeLocalCoffeeSafeLocalCoffeeSafeLocalCoffeeSafeLocalCoff";

function generateTypeScript(): string {
  const lines: string[] = [
    "// Auto-generated from PandaDoc API docs.",
    `// Source: ${DOCS_URL}`,
    "// Regenerate: cd turbo && pnpm -F @vm0/firewalls-generator generate:pandadoc",
    "//",
    "// DO NOT EDIT THIS FILE MANUALLY.",
    "",
    'import type { FirewallConfig } from "../contracts/firewalls";',
    "",
    "export const pandadocFirewall = {",
    '  name: "pandadoc",',
    '  description: "PandaDoc Public API",',
    "  placeholders: {",
    `    PANDADOC_TOKEN: "${PLACEHOLDER_VALUE}",`,
    "  },",
    "  apis: [",
    "    {",
    '      base: "https://api.pandadoc.com",',
    "      auth: {",
    "        headers: {",
    '          Authorization: "API-Key ${{ secrets.PANDADOC_TOKEN }}",',
    "        },",
    "      },",
    "      permissions: [],",
    "    },",
    "  ],",
    "} as const satisfies FirewallConfig;",
    "",
  ];
  return lines.join("\n");
}

export async function generate(): Promise<void> {
  console.error("Generating PandaDoc firewall config...");
  const ts = generateTypeScript();
  writeOutput("pandadoc", ts, import.meta.dirname);
}
