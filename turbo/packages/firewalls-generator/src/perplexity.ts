/**
 * Perplexity API firewall config generator.
 *
 * Data source: https://docs.perplexity.ai/docs/admin/api-key-management
 *
 * Perplexity uses Bearer token authentication.
 * API key format: pplx- prefix + 48 alphanumeric chars = 53 total.
 */

import { writeOutput } from "./codegen";

const DOCS_URL = "https://docs.perplexity.ai/docs/admin/api-key-management";
// Format: pplx- + 48 alphanumeric (gitleaks: perplexity-api-key)
const PLACEHOLDER_VALUE =
  "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";

function generateTypeScript(): string {
  const lines: string[] = [
    "// Auto-generated from Perplexity API docs.",
    `// Source: ${DOCS_URL}`,
    "// Regenerate: cd turbo && pnpm -F @vm0/firewalls-generator generate:perplexity",
    "//",
    "// DO NOT EDIT THIS FILE MANUALLY.",
    "",
    'import type { FirewallConfig } from "../firewall-types";',
    "",
    "export const perplexityFirewall = {",
    '  name: "perplexity",',
    '  description: "Perplexity API",',
    "  placeholders: {",
    `    PERPLEXITY_TOKEN: "${PLACEHOLDER_VALUE}",`,
    "  },",
    "  apis: [",
    "    {",
    '      base: "https://api.perplexity.ai",',
    "      auth: {",
    "        headers: {",
    '          Authorization: "Bearer ${{ secrets.PERPLEXITY_TOKEN }}",',
    "        },",
    "      },",
    "      permissions: [],",
    "    },",
    "  ],",
    "} as const satisfies FirewallConfig;",
    "",
  ];
  return lines.join("\n");
}

export async function generate(): Promise<void> {
  console.error("Generating Perplexity firewall config...");
  const ts = generateTypeScript();
  writeOutput("perplexity", ts, import.meta.dirname);
}
