import { writeOutput } from "./codegen";

const DOCS_URL = "https://porkbun.com/api/json/v3/spec";
const API_KEY_PLACEHOLDER =
  "pk1_Coffee5afe10ca1Coffee5afe10ca1Coffee5afe10ca1Coffee5afe10ca";
const SECRET_API_KEY_PLACEHOLDER =
  "sk1_Coffee5afe10ca1Coffee5afe10ca1Coffee5afe10ca1Coffee5afe10ca";

function renderApi(base: string): string[] {
  return [
    "    {",
    `      base: "${base}",`,
    "      auth: {",
    "        headers: {",
    '          "X-API-Key": "${{ secrets.PORKBUN_API_KEY }}",',
    '          "X-Secret-API-Key": "${{ secrets.PORKBUN_SECRET_API_KEY }}",',
    "        },",
    "      },",
    "      permissions: [],",
    "    },",
  ];
}

function generateTypeScript(): string {
  const lines: string[] = [
    "// Auto-generated from Porkbun API docs.",
    `// Source: ${DOCS_URL}`,
    "// Regenerate: cd turbo && pnpm -F @vm0/firewalls-generator generate:porkbun",
    "//",
    "// DO NOT EDIT THIS FILE MANUALLY.",
    "",
    'import type { FirewallConfig } from "../firewall-types";',
    "",
    "export const porkbunFirewall = {",
    '  name: "porkbun",',
    '  description: "Porkbun",',
    "  placeholders: {",
    `    PORKBUN_API_KEY: "${API_KEY_PLACEHOLDER}",`,
    `    PORKBUN_SECRET_API_KEY: "${SECRET_API_KEY_PLACEHOLDER}",`,
    "  },",
    "  apis: [",
    ...renderApi("https://api.porkbun.com/api/json/v3"),
    ...renderApi("https://api-ipv4.porkbun.com/api/json/v3"),
    "  ],",
    "} as const satisfies FirewallConfig;",
    "",
  ];
  return lines.join("\n");
}

export async function generate(): Promise<void> {
  console.error("Generating Porkbun firewall config...");
  const ts = generateTypeScript();
  writeOutput("porkbun", ts, import.meta.dirname);
}
