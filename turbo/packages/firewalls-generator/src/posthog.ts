/**
 * Generate PostHog firewall config.
 *
 * Data source: https://posthog.com/docs/api
 *
 * PostHog uses Bearer token authentication via Authorization header.
 * Personal API key format: phx_ prefix + alphanumeric chars.
 * Two cloud regions: US (us.posthog.com) and EU (eu.posthog.com).
 */

import { writeOutput } from "./codegen";

const DOCS_URL = "https://posthog.com/docs/api";
// Format: phx_ + ~47 base62 chars = ~51 total (variable length, 35 bytes entropy)
const PLACEHOLDER_VALUE = "phx_CoffeeSafeLocalCoffeeSafeLocalCoffeeSafeLocalCo";

function generateTypeScript(): string {
  const lines: string[] = [
    "// Auto-generated from PostHog API docs.",
    `// Source: ${DOCS_URL}`,
    "// Regenerate: cd turbo && pnpm -F @vm0/firewalls-generator generate:posthog",
    "//",
    "// DO NOT EDIT THIS FILE MANUALLY.",
    "",
    'import type { FirewallConfig } from "../firewall-types";',
    "",
    "export const posthogFirewall = {",
    '  name: "posthog",',
    '  description: "PostHog API",',
    "  placeholders: {",
    `    POSTHOG_TOKEN: "${PLACEHOLDER_VALUE}",`,
    "  },",
    "  apis: [",
    "    {",
    '      base: "https://us.posthog.com",',
    "      auth: {",
    "        headers: {",
    '          Authorization: "Bearer ${{ secrets.POSTHOG_TOKEN }}",',
    "        },",
    "      },",
    "      permissions: [],",
    "    },",
    "    {",
    '      base: "https://eu.posthog.com",',
    "      auth: {",
    "        headers: {",
    '          Authorization: "Bearer ${{ secrets.POSTHOG_TOKEN }}",',
    "        },",
    "      },",
    "      permissions: [],",
    "    },",
    "  ],",
    "} as const satisfies FirewallConfig;",
    "",
  ];

  return lines.join("\n");
}

export async function generate(): Promise<void> {
  console.error("Generating PostHog firewall config...");
  const ts = generateTypeScript();
  writeOutput("posthog", ts, import.meta.dirname);
}
