/**
 * Generate Qdrant firewall config.
 *
 * Data source: https://qdrant.tech/documentation/interfaces/#api-reference
 *
 * Qdrant uses api-key header for authentication.
 * The base URL is cluster-specific (Qdrant Cloud or self-hosted).
 * Users must provide a QDRANT_BASE_URL variable.
 *
 * API key format: alphanumeric string, typically 32-64 chars.
 */

import { writeOutput } from "./codegen";

const DOCS_URL = "https://qdrant.tech/documentation/interfaces/#api-reference";
// Format: alphanumeric string, ~40 chars
const PLACEHOLDER_VALUE = "CoffeeSafeLocalCoffeeSafeLocalCoffeeSafeLo";

function generateTypeScript(): string {
  const lines: string[] = [
    "// Auto-generated from Qdrant API docs.",
    `// Source: ${DOCS_URL}`,
    "// Regenerate: cd turbo && pnpm -F @vm0/firewalls-generator generate:qdrant",
    "//",
    "// DO NOT EDIT THIS FILE MANUALLY.",
    "",
    'import type { FirewallConfig } from "../contracts/firewalls";',
    "",
    "export const qdrantFirewall = {",
    '  name: "qdrant",',
    '  description: "Qdrant Vector Database API",',
    "  placeholders: {",
    `    QDRANT_TOKEN: "${PLACEHOLDER_VALUE}",`,
    "  },",
    "  apis: [",
    "    {",
    '      base: "${{ vars.QDRANT_BASE_URL }}",',
    "      auth: {",
    "        headers: {",
    '          "api-key": "${{ secrets.QDRANT_TOKEN }}",',
    "        },",
    "      },",
    "      permissions: [],",
    "    },",
    "  ],",
    "} as const satisfies FirewallConfig;",
    "",
  ];

  return lines.join("\n");
}

export async function generate(): Promise<void> {
  console.error("Generating Qdrant firewall config...");
  const ts = generateTypeScript();
  writeOutput("qdrant", ts, import.meta.dirname);
}
