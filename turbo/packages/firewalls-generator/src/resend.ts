/**
 * Generate Resend firewall config.
 *
 * Data source: https://resend.com/docs/api-reference/api-keys/create-api-key
 *
 * Resend uses Bearer token authentication via Authorization header.
 * Token format: re_ + ~48 alphanumeric chars.
 */

import { writeOutput } from "./codegen";

const DOCS_URL =
  "https://resend.com/docs/api-reference/api-keys/create-api-key";
// Format: re_ + ~33 alphanumeric chars (e.g. re_c1tpEyD8_NKFusih9vKVQknRAQfmFcWCv)
const PLACEHOLDER_VALUE = "re_CoffeeSafeLocalCoffeeSafeLocalCof";

function generateTypeScript(): string {
  const lines: string[] = [
    "// Auto-generated from Resend API docs.",
    `// Source: ${DOCS_URL}`,
    "// Regenerate: cd turbo && pnpm -F @vm0/firewalls-generator generate:resend",
    "//",
    "// DO NOT EDIT THIS FILE MANUALLY.",
    "",
    'import type { FirewallConfig } from "../contracts/firewalls";',
    "",
    "export const resendFirewall = {",
    '  name: "resend",',
    '  description: "Resend API",',
    "  placeholders: {",
    `    RESEND_TOKEN: "${PLACEHOLDER_VALUE}",`,
    "  },",
    "  apis: [",
    "    {",
    '      base: "https://api.resend.com",',
    "      auth: {",
    "        headers: {",
    '          Authorization: "Bearer ${{ secrets.RESEND_TOKEN }}",',
    "        },",
    "      },",
    "      permissions: [],",
    "    },",
    "  ],",
    "} as const satisfies FirewallConfig;",
    "",
  ];

  return lines.join("\n");
}

export async function generate(): Promise<void> {
  console.error("Generating Resend firewall config...");
  const ts = generateTypeScript();
  writeOutput("resend", ts, import.meta.dirname);
}
