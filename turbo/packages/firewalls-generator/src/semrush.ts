/**
 * Semrush API firewall config generator.
 *
 * Data sources:
 * - https://developer.semrush.com/api/get-started/quick-start/
 * - https://developer.semrush.com/api/seo/overview/
 *
 * Semrush authenticates API requests with a `key` URL query parameter.
 * API key format is not publicly documented; using generic placeholder.
 */

import { writeOutput } from "./codegen";

const DOCS_URL = "https://developer.semrush.com/api/get-started/quick-start/";
const PLACEHOLDER_VALUE = "CoffeeSafeLocalCoffeeSafeLocalCoffeeSafeLocalCof";

function generateTypeScript(): string {
  const lines: string[] = [
    "// Auto-generated from Semrush API docs.",
    `// Source: ${DOCS_URL}`,
    "// Regenerate: cd turbo && pnpm -F @vm0/firewalls-generator generate:semrush",
    "//",
    "// DO NOT EDIT THIS FILE MANUALLY.",
    "",
    'import type { FirewallConfig } from "../firewall-types";',
    "",
    "export const semrushFirewall = {",
    '  name: "semrush",',
    '  description: "Semrush API",',
    "  placeholders: {",
    `    SEMRUSH_TOKEN: "${PLACEHOLDER_VALUE}",`,
    "  },",
    "  apis: [",
    "    {",
    '      base: "https://api.semrush.com",',
    "      auth: {",
    "        query: {",
    '          key: "${{ secrets.SEMRUSH_TOKEN }}",',
    "        },",
    "      },",
    "      permissions: [],",
    "    },",
    "  ],",
    "} as const satisfies FirewallConfig;",
    "",
  ];
  return lines.join("\n");
}

export async function generate(): Promise<void> {
  console.error("Generating Semrush firewall config...");
  const ts = generateTypeScript();
  writeOutput("semrush", ts, import.meta.dirname);
}
