/**
 * Generate SendGrid firewall config.
 *
 * Data source: https://www.twilio.com/docs/sendgrid/api-reference
 *
 * SendGrid uses Bearer authentication on every v3 endpoint. The token
 * always starts with the literal prefix `SG.`.
 */

import { writeOutput } from "./codegen";

const DOCS_URL =
  "https://www.twilio.com/docs/sendgrid/api-reference/how-to-use-the-sendgrid-v3-api/authentication";
// SendGrid API keys are ~69 chars starting with "SG.". Format is
// `SG.<22 chars>.<43 chars>` — fill with Coffee/Safe/Local Base62 vocab.
const PLACEHOLDER_VALUE =
  "SG.CoffeeSafeLocalCoffeeS.CoffeeSafeLocalCoffeeSafeLocalCoffeeSafeLoca";

function generateTypeScript(): string {
  const lines: string[] = [
    "// Auto-generated from SendGrid API docs.",
    `// Source: ${DOCS_URL}`,
    "// Regenerate: cd turbo && pnpm -F @vm0/firewalls-generator generate:sendgrid",
    "//",
    "// DO NOT EDIT THIS FILE MANUALLY.",
    "",
    'import type { FirewallConfig } from "../firewall-types";',
    "",
    "export const sendgridFirewall = {",
    '  name: "sendgrid",',
    '  description: "Twilio SendGrid email API",',
    "  placeholders: {",
    `    SENDGRID_TOKEN: "${PLACEHOLDER_VALUE}",`,
    "  },",
    "  apis: [",
    "    {",
    '      base: "https://api.sendgrid.com",',
    "      auth: {",
    "        headers: {",
    '          Authorization: "Bearer ${{ secrets.SENDGRID_TOKEN }}",',
    "        },",
    "      },",
    "      permissions: [],",
    "    },",
    "  ],",
    "} as const satisfies FirewallConfig;",
    "",
  ];

  return lines.join("\n");
}

export async function generate(): Promise<void> {
  console.error("Generating SendGrid firewall config...");
  const ts = generateTypeScript();
  writeOutput("sendgrid", ts, import.meta.dirname);
}
