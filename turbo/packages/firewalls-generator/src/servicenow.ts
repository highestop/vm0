/**
 * Generate ServiceNow firewall config.
 *
 * Data source: https://developer.servicenow.com/dev.do#!/reference/api/rome/rest/c_TableAPI
 *
 * ServiceNow uses HTTP Basic auth with a platform user's username +
 * password against an instance-specific host (<subdomain>.service-now.com).
 * The subdomain is supplied as a variable.
 */

import { writeOutput } from "./codegen";

const DOCS_URL =
  "https://developer.servicenow.com/dev.do#!/reference/api/rome/rest/c_TableAPI";
const USERNAME_PLACEHOLDER = "coffee.safe.local";
const PASSWORD_PLACEHOLDER = "CoffeeSafeLocalCoffeeSafe";

function generateTypeScript(): string {
  const lines: string[] = [
    "// Auto-generated from ServiceNow API docs.",
    `// Source: ${DOCS_URL}`,
    "// Regenerate: cd turbo && pnpm -F @vm0/firewalls-generator generate:servicenow",
    "//",
    "// DO NOT EDIT THIS FILE MANUALLY.",
    "",
    'import type { FirewallConfig } from "../firewall-types";',
    "",
    "export const servicenowFirewall = {",
    '  name: "servicenow",',
    '  description: "ServiceNow Now Platform Table and Aggregate API",',
    "  placeholders: {",
    `    SERVICENOW_USERNAME: "${USERNAME_PLACEHOLDER}",`,
    `    SERVICENOW_PASSWORD: "${PASSWORD_PLACEHOLDER}",`,
    "  },",
    "  apis: [",
    "    {",
    '      base: "https://${{ vars.SERVICENOW_INSTANCE }}.service-now.com",',
    "      auth: {",
    "        headers: {",
    "          Authorization:",
    '            "${{ basic(secrets.SERVICENOW_USERNAME, secrets.SERVICENOW_PASSWORD) }}",',
    "        },",
    "      },",
    "      permissions: [],",
    "    },",
    "  ],",
    "} as const satisfies FirewallConfig;",
    "",
  ];

  return lines.join("\n");
}

export async function generate(): Promise<void> {
  console.error("Generating ServiceNow firewall config...");
  const ts = generateTypeScript();
  writeOutput("servicenow", ts, import.meta.dirname);
}
