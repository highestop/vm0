/**
 * Generate Shopify firewall config.
 *
 * Data source: https://shopify.dev/docs/api/admin-rest
 *
 * Shopify (custom-app auth) uses a long-lived Admin API access token
 * passed in the X-Shopify-Access-Token header.  The base URL is
 * per-store: https://{shop}.myshopify.com — the shop subdomain is
 * provided as a variable (${{ vars.SHOPIFY_SHOP }}).
 *
 * Token format (gitleaks: shopify-access-token): shpat_[a-fA-F0-9]{32}
 *
 * The placeholder intentionally uses base62 vocabulary (non-hex letters)
 * so it does NOT match GitHubs secret-scanning regex for real tokens.
 */

import { writeOutput } from "./codegen";

const DOCS_URL = "https://shopify.dev/docs/api/admin-rest";
// Format: shpat_ + 32 chars. Real tokens are hex-only; this uses mixed-case
// base62 (Coffee/Safe/Local) to avoid secret-scanning false positives.
const PLACEHOLDER_VALUE = "shpat_CoffeeSafeLocalCoffeeSafeLocalCo";

function generateTypeScript(): string {
  const lines: string[] = [
    "// Auto-generated from Shopify Admin API docs.",
    `// Source: ${DOCS_URL}`,
    "// Regenerate: cd turbo && pnpm -F @vm0/firewalls-generator generate:shopify",
    "//",
    "// DO NOT EDIT THIS FILE MANUALLY.",
    "",
    'import type { FirewallConfig } from "../firewall-types";',
    "",
    "export const shopifyFirewall = {",
    '  name: "shopify",',
    '  description: "Shopify Admin API",',
    "  placeholders: {",
    `    SHOPIFY_TOKEN: "${PLACEHOLDER_VALUE}",`,
    "  },",
    "  apis: [",
    "    {",
    '      base: "https://${{ vars.SHOPIFY_SHOP }}.myshopify.com",',
    "      auth: {",
    "        headers: {",
    '          "X-Shopify-Access-Token": "${{ secrets.SHOPIFY_TOKEN }}",',
    "        },",
    "      },",
    "      permissions: [],",
    "    },",
    "  ],",
    "} as const satisfies FirewallConfig;",
    "",
  ];

  return lines.join("\n");
}

export async function generate(): Promise<void> {
  console.error("Generating Shopify firewall config...");
  const ts = generateTypeScript();
  writeOutput("shopify", ts, import.meta.dirname);
}
