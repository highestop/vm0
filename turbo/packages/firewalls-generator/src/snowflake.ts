/**
 * Generate Snowflake firewall config.
 *
 * Data sources:
 * - https://docs.snowflake.com/en/developer-guide/snowflake-rest-api/authentication
 * - https://docs.snowflake.com/en/developer-guide/sql-api/about-endpoints
 *
 * Snowflake Programmatic Access Tokens authenticate REST API and SQL API
 * requests with a Bearer token. The account identifier is tenant-specific.
 */

import { writeOutput } from "./codegen";

const DOCS_URL =
  "https://docs.snowflake.com/en/developer-guide/snowflake-rest-api/authentication";
const SQL_API_URL =
  "https://docs.snowflake.com/en/developer-guide/sql-api/about-endpoints";
const PLACEHOLDER_VALUE =
  "CoffeeSafeLocalCoffeeSafeLocalCoffeeSafeLocalCoffeeSafeLocal";

function generateTypeScript(): string {
  const lines: string[] = [
    "// Auto-generated from Snowflake REST API and SQL API docs.",
    `// Sources: ${DOCS_URL}`,
    `//          ${SQL_API_URL}`,
    "// Regenerate: cd turbo && pnpm -F @vm0/firewalls-generator generate:snowflake",
    "//",
    "// DO NOT EDIT THIS FILE MANUALLY.",
    "",
    'import type { FirewallConfig } from "../firewall-types";',
    "",
    "export const snowflakeFirewall = {",
    '  name: "snowflake",',
    '  description: "Snowflake REST API and SQL API",',
    "  placeholders: {",
    `    SNOWFLAKE_PAT: "${PLACEHOLDER_VALUE}",`,
    "  },",
    "  apis: [",
    "    {",
    '      base: "https://${{ vars.SNOWFLAKE_ACCOUNT }}.snowflakecomputing.com/api",',
    "      auth: {",
    "        headers: {",
    '          Authorization: "Bearer ${{ secrets.SNOWFLAKE_PAT }}",',
    '          "X-Snowflake-Authorization-Token-Type": "PROGRAMMATIC_ACCESS_TOKEN",',
    "        },",
    "      },",
    "      permissions: [],",
    "    },",
    "  ],",
    "} as const satisfies FirewallConfig;",
    "",
  ];

  return lines.join("\n");
}

export async function generate(): Promise<void> {
  console.error("Generating Snowflake firewall config...");
  const ts = generateTypeScript();
  writeOutput("snowflake", ts, import.meta.dirname);
}
