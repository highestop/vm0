/**
 * Generate Sponge firewall config.
 *
 * Sponge is the financial infrastructure for the agent economy
 * (wallets, cards, bank accounts, x402/MPP payments). Authentication
 * is Bearer with a master key (`sponge_master_…`) at the platform
 * layer — scoped agent keys minted at runtime use the same header
 * shape against the same host.
 */

import { writeOutput } from "./codegen";

const DOCS_URL = "https://docs.paysponge.com";
// Master keys carry the `sponge_master_` prefix per Sponge docs; fill the
// remainder with the Coffee/Safe/Local vocabulary so the placeholder stays
// format-correct without leaking a real key shape.
const PLACEHOLDER_VALUE = "sponge_master_CoffeeSafeLocalCoffeeSafeLocalCoffee";

function generateTypeScript(): string {
  const lines: string[] = [
    "// Auto-generated from Sponge wallet API docs.",
    `// Source: ${DOCS_URL}`,
    "// Regenerate: cd turbo && pnpm -F @vm0/firewalls-generator generate:sponge",
    "//",
    "// DO NOT EDIT THIS FILE MANUALLY.",
    "",
    'import type { FirewallConfig } from "../firewall-types";',
    "",
    "export const spongeFirewall = {",
    '  name: "sponge",',
    '  description: "Sponge wallet, cards, transfers, x402/MPP payments",',
    "  placeholders: {",
    `    SPONGE_MASTER_KEY: "${PLACEHOLDER_VALUE}",`,
    "  },",
    "  apis: [",
    "    {",
    '      base: "https://api.wallet.paysponge.com",',
    "      auth: {",
    "        headers: {",
    '          Authorization: "Bearer ${{ secrets.SPONGE_MASTER_KEY }}",',
    "        },",
    "      },",
    "      permissions: [],",
    "    },",
    "  ],",
    "} as const satisfies FirewallConfig;",
    "",
  ];
  return lines.join("\n");
}

export async function generate(): Promise<void> {
  console.error("Generating Sponge firewall config...");
  const ts = generateTypeScript();
  writeOutput("sponge", ts, import.meta.dirname);
}
