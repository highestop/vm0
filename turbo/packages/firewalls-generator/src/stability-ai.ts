/**
 * Stability AI API firewall config generator.
 *
 * Data source: https://platform.stability.ai/docs/api-reference
 *
 * Stability AI uses Bearer token auth. API keys start with "sk-".
 * Token format: sk- followed by alphanumeric characters (~43 chars total).
 */

import { writeOutput } from "./codegen";

const DOCS_URL = "https://platform.stability.ai/docs/api-reference";
// Format: sk- followed by alphanumeric (~43 chars total)
const PLACEHOLDER_VALUE = "sk-CoffeeSafeLocalCoffeeSafeLocalCoffeeSafeLocal";

function generateTypeScript(): string {
  const lines: string[] = [
    "// Auto-generated from Stability AI API docs.",
    `// Source: ${DOCS_URL}`,
    "// Regenerate: cd turbo && pnpm -F @vm0/firewalls-generator generate:stability-ai",
    "//",
    "// DO NOT EDIT THIS FILE MANUALLY.",
    "",
    'import type { FirewallConfig } from "../contracts/firewalls";',
    "",
    "export const stabilityAiFirewall = {",
    '  name: "stability-ai",',
    '  description: "Stability AI",',
    "  placeholders: {",
    `    STABILITY_TOKEN: "${PLACEHOLDER_VALUE}",`,
    "  },",
    "  apis: [",
    "    {",
    '      base: "https://api.stability.ai",',
    "      auth: {",
    "        headers: {",
    '          Authorization: "Bearer ${{ secrets.STABILITY_TOKEN }}",',
    "        },",
    "      },",
    "      permissions: [],",
    "    },",
    "  ],",
    "} as const satisfies FirewallConfig;",
    "",
  ];
  return lines.join("\n");
}

export async function generate(): Promise<void> {
  console.error("Generating Stability AI firewall config...");
  const ts = generateTypeScript();
  writeOutput("stability-ai", ts, import.meta.dirname);
}
