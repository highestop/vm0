/**
 * Generate Strava firewall config.
 *
 * Data source: https://developers.strava.com/docs/reference/
 *
 * Strava is a fitness tracking platform.
 * Uses Bearer token authentication (OAuth).
 * Token format: OAuth access token, opaque string, expires in 6 hours.
 * Only unrestricted permissions for now.
 */

import { writeOutput } from "./codegen";

const DOCS_URL = "https://developers.strava.com/docs/reference/";
// OAuth token; using generic placeholder
const PLACEHOLDER_VALUE =
  "vm0placeholder00000000000000000000000000000000000000000000000000a";

function generateTypeScript(): string {
  const lines: string[] = [
    "// Auto-generated from Strava API docs.",
    `// Source: ${DOCS_URL}`,
    "// Regenerate: cd turbo && pnpm -F @vm0/firewalls-generator generate:strava",
    "//",
    "// DO NOT EDIT THIS FILE MANUALLY.",
    "",
    'import type { FirewallConfig } from "../contracts/firewalls";',
    "",
    "export const stravaFirewall: FirewallConfig = {",
    '  name: "strava",',
    '  description: "Strava API",',
    "  placeholders: {",
    `    STRAVA_TOKEN: "${PLACEHOLDER_VALUE}",`,
    "  },",
    "  apis: [",
    "    {",
    '      base: "https://www.strava.com",',
    "      auth: {",
    "        headers: {",
    '          Authorization: "Bearer ${{ secrets.STRAVA_TOKEN }}",',
    "        },",
    "      },",
    "      permissions: [",
    "        {",
    '          name: "unrestricted",',
    '          description: "Allow all endpoints",',
    '          rules: ["ANY /{path*}"],',
    "        },",
    "      ],",
    "    },",
    "  ],",
    "};",
    "",
  ];

  return lines.join("\n");
}

export async function generate(): Promise<void> {
  console.error("Generating Strava firewall config...");
  const ts = generateTypeScript();
  writeOutput("strava", ts, import.meta.dirname);
}
