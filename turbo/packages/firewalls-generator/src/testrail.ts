/**
 * Generate TestRail firewall config.
 *
 * Data source: https://support.testrail.com/hc/en-us/articles/7077039051284-Accessing-the-TestRail-API
 *
 * TestRail uses HTTP Basic auth with the user's email as username and a
 * generated API key as password. Hosted instances all live on
 * <subdomain>.testrail.io — the subdomain is supplied as a variable.
 */

import { writeOutput } from "./codegen";

const DOCS_URL =
  "https://support.testrail.com/hc/en-us/articles/7077039051284-Accessing-the-TestRail-API";
const EMAIL_PLACEHOLDER = "coffee@safe-local.example.com";
const TOKEN_PLACEHOLDER = "CoffeeSafeLocalCoffeeSafeLocal";

function generateTypeScript(): string {
  const lines: string[] = [
    "// Auto-generated from TestRail API docs.",
    `// Source: ${DOCS_URL}`,
    "// Regenerate: cd turbo && pnpm -F @vm0/firewalls-generator generate:testrail",
    "//",
    "// DO NOT EDIT THIS FILE MANUALLY.",
    "",
    'import type { FirewallConfig } from "../firewall-types";',
    "",
    "export const testrailFirewall = {",
    '  name: "testrail",',
    '  description: "TestRail QA test case management API",',
    "  placeholders: {",
    `    TESTRAIL_EMAIL: "${EMAIL_PLACEHOLDER}",`,
    `    TESTRAIL_TOKEN: "${TOKEN_PLACEHOLDER}",`,
    "  },",
    "  apis: [",
    "    {",
    '      base: "https://${{ vars.TESTRAIL_INSTANCE }}.testrail.io",',
    "      auth: {",
    "        headers: {",
    "          Authorization:",
    '            "${{ basic(secrets.TESTRAIL_EMAIL, secrets.TESTRAIL_TOKEN) }}",',
    "        },",
    "      },",
    "      permissions: [],",
    "    },",
    "  ],",
    "} as const satisfies FirewallConfig;",
    "",
  ];

  return lines.join("\n");
}

export async function generate(): Promise<void> {
  console.error("Generating TestRail firewall config...");
  const ts = generateTypeScript();
  writeOutput("testrail", ts, import.meta.dirname);
}
