/**
 * Generate Todoist firewall config.
 *
 * Data source: https://developer.todoist.com/guides/
 *
 * Todoist uses Bearer token authentication via Authorization header.
 * Token format: 40-char hex string (e.g. 0123456789abcdef0123456789abcdef01234567).
 */

import { writeOutput } from "./codegen";

const DOCS_URL = "https://developer.todoist.com/guides/";
// Format: 40-char hex string
// Hex-only format, using c0ffee5afe10ca1 pattern
const PLACEHOLDER_VALUE = "c0ffee5afe10ca1c0ffee5afe10ca1c0ffee5af";

function generateTypeScript(): string {
  const lines: string[] = [
    "// Auto-generated from Todoist API docs.",
    `// Source: ${DOCS_URL}`,
    "// Regenerate: cd turbo && pnpm -F @vm0/firewalls-generator generate:todoist",
    "//",
    "// DO NOT EDIT THIS FILE MANUALLY.",
    "",
    'import type { FirewallConfig } from "../firewall-types";',
    "",
    "export const todoistFirewall = {",
    '  name: "todoist",',
    '  description: "Todoist API",',
    "  placeholders: {",
    `    TODOIST_TOKEN: "${PLACEHOLDER_VALUE}",`,
    "  },",
    "  apis: [",
    "    {",
    '      base: "https://api.todoist.com",',
    "      auth: {",
    "        headers: {",
    '          Authorization: "Bearer ${{ secrets.TODOIST_TOKEN }}",',
    "        },",
    "      },",
    "      permissions: [],",
    "    },",
    "  ],",
    "} as const satisfies FirewallConfig;",
    "",
  ];

  return lines.join("\n");
}

export async function generate(): Promise<void> {
  console.error("Generating Todoist firewall config...");
  const ts = generateTypeScript();
  writeOutput("todoist", ts, import.meta.dirname);
}
