/**
 * Generate Tripo 3D firewall config.
 *
 * Data source: https://platform.tripo3d.ai/docs/introduction
 *
 * Tripo uses Bearer token authentication via the Authorization header.
 * A single API host (api.tripo3d.ai) serves the whole OpenAPI surface
 * (task submission, polling, upload, generation, texture, animation,
 * post-process, wallet), so a single host entry covers everything.
 * Token format: `tsk_`-prefixed string.
 */

import { writeOutput } from "./codegen";

const DOCS_URL = "https://platform.tripo3d.ai/docs/introduction";
// `tsk_`-prefixed placeholder using the Coffee/Safe/Local base62 vocabulary so
// the proxy regex still matches without leaking a real key shape.
const PLACEHOLDER_VALUE = "tsk_CoffeeSafeLocalCoffeeSafeLocalCoffeeSafeLoc";

function generateTypeScript(): string {
  const lines: string[] = [
    "// Auto-generated from Tripo 3D API docs.",
    `// Source: ${DOCS_URL}`,
    "// Regenerate: cd turbo && pnpm -F @vm0/firewalls-generator generate:tripo",
    "//",
    "// DO NOT EDIT THIS FILE MANUALLY.",
    "",
    'import type { FirewallConfig } from "../firewall-types";',
    "",
    "export const tripoFirewall = {",
    '  name: "tripo",',
    '  description: "Tripo",',
    "  placeholders: {",
    `    TRIPO_API_KEY: "${PLACEHOLDER_VALUE}",`,
    "  },",
    "  apis: [",
    "    {",
    '      base: "https://api.tripo3d.ai",',
    "      auth: {",
    "        headers: {",
    '          Authorization: "Bearer ${{ secrets.TRIPO_API_KEY }}",',
    "        },",
    "      },",
    "      permissions: [],",
    "    },",
    "  ],",
    "} as const satisfies FirewallConfig;",
    "",
  ];

  return lines.join("\n");
}

export async function generate(): Promise<void> {
  console.error("Generating Tripo 3D firewall config...");
  const ts = generateTypeScript();
  writeOutput("tripo", ts, import.meta.dirname);
}
