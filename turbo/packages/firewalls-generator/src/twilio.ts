/**
 * Generate Twilio firewall config.
 *
 * Data source: https://www.twilio.com/docs/iam/api
 *
 * Twilio uses HTTP Basic auth with the Account SID as username and Auth
 * Token as password. The same credentials are accepted on the classic
 * REST host (api.twilio.com) and the product-specific subdomains
 * (verify, lookups, messaging, studio, serverless).
 *
 * Account SIDs always start with "AC" and are 34 chars total; auth
 * tokens are 32-char hex strings.
 */

import { writeOutput } from "./codegen";

const DOCS_URL = "https://www.twilio.com/docs/iam/api";
// 34-char Account SID: "AC" + 32 base62-style chars built from Coffee/Safe/Local.
const ACCOUNT_SID_PLACEHOLDER = "ACCoffeeSafeLocalCoffeeSafeLocalCo";
// 32-char hex auth token.
const AUTH_TOKEN_PLACEHOLDER = "c0ffee5afe10ca1c0ffee5afe10ca1c0";

function generateTypeScript(): string {
  const lines: string[] = [
    "// Auto-generated from Twilio API docs.",
    `// Source: ${DOCS_URL}`,
    "// Regenerate: cd turbo && pnpm -F @vm0/firewalls-generator generate:twilio",
    "//",
    "// DO NOT EDIT THIS FILE MANUALLY.",
    "",
    'import type { FirewallConfig } from "../firewall-types";',
    "",
    "export const twilioFirewall = {",
    '  name: "twilio",',
    '  description: "Twilio messaging, voice, and verification API",',
    "  placeholders: {",
    `    TWILIO_ACCOUNT_SID: "${ACCOUNT_SID_PLACEHOLDER}",`,
    `    TWILIO_AUTH_TOKEN: "${AUTH_TOKEN_PLACEHOLDER}",`,
    "  },",
    "  apis: [",
    "    {",
    '      base: "https://api.twilio.com",',
    "      auth: {",
    "        headers: {",
    "          Authorization:",
    '            "${{ basic(secrets.TWILIO_ACCOUNT_SID, secrets.TWILIO_AUTH_TOKEN) }}",',
    "        },",
    "      },",
    "      permissions: [],",
    "    },",
    "    {",
    '      base: "https://verify.twilio.com",',
    "      auth: {",
    "        headers: {",
    "          Authorization:",
    '            "${{ basic(secrets.TWILIO_ACCOUNT_SID, secrets.TWILIO_AUTH_TOKEN) }}",',
    "        },",
    "      },",
    "      permissions: [],",
    "    },",
    "    {",
    '      base: "https://lookups.twilio.com",',
    "      auth: {",
    "        headers: {",
    "          Authorization:",
    '            "${{ basic(secrets.TWILIO_ACCOUNT_SID, secrets.TWILIO_AUTH_TOKEN) }}",',
    "        },",
    "      },",
    "      permissions: [],",
    "    },",
    "    {",
    '      base: "https://messaging.twilio.com",',
    "      auth: {",
    "        headers: {",
    "          Authorization:",
    '            "${{ basic(secrets.TWILIO_ACCOUNT_SID, secrets.TWILIO_AUTH_TOKEN) }}",',
    "        },",
    "      },",
    "      permissions: [],",
    "    },",
    "    {",
    '      base: "https://studio.twilio.com",',
    "      auth: {",
    "        headers: {",
    "          Authorization:",
    '            "${{ basic(secrets.TWILIO_ACCOUNT_SID, secrets.TWILIO_AUTH_TOKEN) }}",',
    "        },",
    "      },",
    "      permissions: [],",
    "    },",
    "    {",
    '      base: "https://serverless.twilio.com",',
    "      auth: {",
    "        headers: {",
    "          Authorization:",
    '            "${{ basic(secrets.TWILIO_ACCOUNT_SID, secrets.TWILIO_AUTH_TOKEN) }}",',
    "        },",
    "      },",
    "      permissions: [],",
    "    },",
    "  ],",
    "} as const satisfies FirewallConfig;",
    "",
  ];

  return lines.join("\n");
}

export async function generate(): Promise<void> {
  console.error("Generating Twilio firewall config...");
  const ts = generateTypeScript();
  writeOutput("twilio", ts, import.meta.dirname);
}
