/**
 * Generate WeRead (微信读书) firewall config.
 *
 * Data source: https://weread.qq.com/r/weread-skills
 *
 * WeRead's official Agent Skill exposes every capability through a single
 * gateway endpoint, `POST https://i.weread.qq.com/api/agent/gateway`. The
 * `api_name` field in the JSON body selects the sub-API. Auth is a static
 * bearer token (`Authorization: Bearer wrk-...`), so a single host entry
 * covers the whole surface.
 */

import { writeOutput } from "./codegen";

const DOCS_URL = "https://weread.qq.com/r/weread-skills";
// `wrk-`-prefixed placeholder using the Coffee/Safe/Local base62 vocabulary so
// the proxy regex still matches without leaking a real key shape.
const PLACEHOLDER_VALUE = "wrk-CoffeeSafeLocalCoffeeSafeLocalCoffee";

function generateTypeScript(): string {
  const lines: string[] = [
    "// Auto-generated from the WeRead Agent Skill gateway docs.",
    `// Source: ${DOCS_URL}`,
    "// Regenerate: cd turbo && pnpm -F @vm0/firewalls-generator generate:weread",
    "//",
    "// DO NOT EDIT THIS FILE MANUALLY.",
    "",
    'import type { FirewallConfig } from "../firewall-types";',
    "",
    "export const wereadFirewall = {",
    '  name: "weread",',
    '  description: "WeRead (微信读书) Agent gateway: bookshelf, notes, reading stats, search",',
    "  placeholders: {",
    `    WEREAD_API_KEY: "${PLACEHOLDER_VALUE}",`,
    "  },",
    "  apis: [",
    "    {",
    '      base: "https://i.weread.qq.com",',
    "      auth: {",
    "        headers: {",
    '          Authorization: "Bearer ${{ secrets.WEREAD_API_KEY }}",',
    "        },",
    "      },",
    "      permissions: [],",
    "    },",
    "  ],",
    "} as const satisfies FirewallConfig;",
    "",
  ];

  return lines.join("\n");
}

export async function generate(): Promise<void> {
  console.error("Generating WeRead firewall config...");
  const ts = generateTypeScript();
  writeOutput("weread", ts, import.meta.dirname);
}
