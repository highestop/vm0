/**
 * Generate Wrike firewall config.
 *
 * Data source: https://developers.wrike.com/overview/
 *
 * Wrike uses datacenter-specific domains:
 * www.wrike.com (US default), app-us2.wrike.com (US2), app-eu.wrike.com (EU).
 * All regions share the same Bearer token auth.
 */

import { writeOutput } from "./codegen";

const DOCS_URL = "https://developers.wrike.com/overview/";
// Wrike permanent access tokens are opaque alphanumeric strings
const PLACEHOLDER_VALUE = "wrkTkn_CoffeeSafeLocalCoffeeSafeLocalCoffeeSafeLoca";

const WRIKE_DOMAINS = [
  "www.wrike.com",
  "app-us2.wrike.com",
  "app-eu.wrike.com",
];

function generateTypeScript(): string {
  const apiEntries = WRIKE_DOMAINS.map(
    (domain) => `    {
      base: "https://${domain}",
      auth: {
        headers: {
          Authorization: "bearer $\{{ secrets.WRIKE_TOKEN }}",
        },
      },
      permissions: [],
    }`,
  ).join(",\n");

  const lines: string[] = [
    "// Auto-generated from Wrike API docs.",
    `// Source: ${DOCS_URL}`,
    "// Regenerate: cd turbo && pnpm -F @vm0/firewalls-generator generate:wrike",
    "//",
    "// DO NOT EDIT THIS FILE MANUALLY.",
    "",
    'import type { FirewallConfig } from "../firewall-types";',
    "",
    "export const wrikeFirewall = {",
    '  name: "wrike",',
    '  description: "Wrike Project Management API",',
    "  placeholders: {",
    `    WRIKE_TOKEN: "${PLACEHOLDER_VALUE}",`,
    "  },",
    "  apis: [",
    apiEntries + ",",
    "  ],",
    "} as const satisfies FirewallConfig;",
    "",
  ];

  return lines.join("\n");
}

export async function generate(): Promise<void> {
  console.error("Generating Wrike firewall config...");
  const ts = generateTypeScript();
  writeOutput("wrike", ts, import.meta.dirname);
}
