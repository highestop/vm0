/**
 * Generate YouTube firewall config.
 *
 * Data source: https://developers.google.com/youtube/v3/getting-started
 *
 * YouTube Data API v3 uses Google Cloud API keys.
 * Auth via query parameter (key=), not header, but we use header for proxy.
 * Only unrestricted permissions for now.
 */

import { writeOutput } from "./codegen";

const DOCS_URL = "https://developers.google.com/youtube/v3/getting-started";
// Format: AIza + 35 word chars (gitleaks: gcp-api-key)
// e.g. AIzaSyBVm0PlaceHolder00000000000000000
const PLACEHOLDER_VALUE = "AIzaSyBVm0PlaceHolder000000000000000000";

function generateTypeScript(): string {
  const lines: string[] = [
    "// Auto-generated from YouTube Data API docs.",
    `// Source: ${DOCS_URL}`,
    "// Regenerate: cd turbo && pnpm -F @vm0/firewalls-generator generate:youtube",
    "//",
    "// DO NOT EDIT THIS FILE MANUALLY.",
    "",
    'import type { FirewallConfig } from "../contracts/firewalls";',
    "",
    "export const youtubeFirewall: FirewallConfig = {",
    '  name: "youtube",',
    '  description: "YouTube Data API",',
    "  placeholders: {",
    `    YOUTUBE_TOKEN: "${PLACEHOLDER_VALUE}",`,
    "  },",
    "  apis: [",
    "    {",
    '      base: "https://www.googleapis.com/youtube",',
    "      auth: {",
    "        headers: {",
    '          "X-Goog-Api-Key": "${{ secrets.YOUTUBE_TOKEN }}",',
    "        },",
    "      },",
    "      permissions: [",
    "        {",
    '          name: "unrestricted",',
    '          description: "Allow all endpoints",',
    '          rules: ["ANY /{path*}"],',
    "        },",
    "      ],",
    "    },",
    "  ],",
    "};",
    "",
  ];

  return lines.join("\n");
}

export async function generate(): Promise<void> {
  console.error("Generating YouTube firewall config...");
  const ts = generateTypeScript();
  writeOutput("youtube", ts, import.meta.dirname);
}
