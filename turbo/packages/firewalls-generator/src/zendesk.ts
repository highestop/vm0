/**
 * Generate Zendesk firewall config.
 *
 * Data source: https://developer.zendesk.com/api-reference/
 *
 * Zendesk is a customer service and support platform.
 * Base URL is subdomain-based: https://{subdomain}.zendesk.com
 * The subdomain is provided as a variable (${{ vars.ZENDESK_SUBDOMAIN }}).
 */

import { writeOutput } from "./codegen";

const DOCS_URL = "https://developer.zendesk.com/api-reference/";
// Format: Zendesk API tokens are 40-char alphanumeric
const PLACEHOLDER_VALUE = "zkTkn_CoffeeSafeLocalCoffeeSafeLocalCoffeeSa";

function generateTypeScript(): string {
  const lines: string[] = [
    "// Auto-generated from Zendesk API docs.",
    `// Source: ${DOCS_URL}`,
    "// Regenerate: cd turbo && pnpm -F @vm0/firewalls-generator generate:zendesk",
    "//",
    "// DO NOT EDIT THIS FILE MANUALLY.",
    "",
    'import type { FirewallConfig } from "../contracts/firewalls";',
    "",
    "export const zendeskFirewall = {",
    '  name: "zendesk",',
    '  description: "Zendesk Support API",',
    "  placeholders: {",
    `    ZENDESK_API_TOKEN: "${PLACEHOLDER_VALUE}",`,
    "  },",
    "  apis: [",
    "    {",
    '      base: "https://${{ vars.ZENDESK_SUBDOMAIN }}.zendesk.com",',
    "      auth: {",
    "        headers: {",
    '          Authorization: "Bearer ${{ secrets.ZENDESK_API_TOKEN }}",',
    "        },",
    "      },",
    "      permissions: [],",
    "    },",
    "  ],",
    "} as const satisfies FirewallConfig;",
    "",
  ];

  return lines.join("\n");
}

export async function generate(): Promise<void> {
  console.error("Generating Zendesk firewall config...");
  const ts = generateTypeScript();
  writeOutput("zendesk", ts, import.meta.dirname);
}
